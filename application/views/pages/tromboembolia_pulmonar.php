                     <h6><span><i class="fas fa-heart"></i></span>TROMBOEMBOLIA PULMONAR</h6>
                  </div>
                  <div class="row " style="text-align: justify;">
                     <div class="col-lg-12">
                        <p>
                           Escrito por el Dr. Alejandro López Leija. Medicina Interna.
                        </p>
                        <p>
                           <br>El presente artículo respornderá las principales preguntas relacionadas con la
                           tromboembolia pulmonar (TEP). ¿Qué es la TEP? ¿Cómo sucede? ¿Qué factores favorecen que una
                           persona tenga una TEP? ¿Qué síntomas se presentan? ¿Cómo se diagnostica? ¿Cómo se trata? y
                           finalmente ¿Cuál es el pronóstico después de haber tenido una TEP?
                        </p>

                        <h1><br>¿Qué es la TEP? </h1>

                        <p>
                           <br>Al hablar de tromboembolia pulmonar nos referimos a la obstrucción del flujo sanguíneo de
                           las arterias pulmonares por causa de un coágulo o trombo dentro de las mismas, frecuentemente
                           proveniente desde la circulación venosa de las extremidades inferiores. Las arterias
                           pulmonares conectan el corazón con los pulmones para que se lleve a cabo la oxigenación de la
                           sangre y posteriormente pueda ser bombeada al resto del cuerpo. La tromboembolia pulmonar
                           genera un importante impacto sobre la salud de forma súbita debido a que produce en primer
                           lugar una disminución en la capacidad de oxigenación de la sangre y en segundo lugar genera
                           un aumento en la presión de las arterias pulmonares y con ello la necesidad de un mayor
                           esfuerzo del corazón para bombear la sangre a los pulmones. Es importante decir que la
                           porción derecha del corazón es la que se encarga del proceso previamente mencionada y que por
                           sus características anatómicas es muy sensible a los cambios de presión en las arterias
                           pulmonares, con lo que se puede producir una falla de esta porción del corazón.
                        </p>

                        <h1><br>¿Cómo sucede la TEP?</h1>
                        <p>
                           <br>La principal causa de tromboembolia pulmonar es la trombosis venosa profunda. Las venas
                           periféricas requieren de la contracción de los músculos para bombear la sangre de regreso al
                           corazón. En situaciones en las que existe poca movilidad muscular como por ejemplo después de
                           una cirugía o después de un viaje largo, el flujo de sangre se enlentece lo cual puede
                           favorecer la formación de un coágulo sobre todo en las venas de las extremidades inferiores.
                           Dicho coágulo puede generar obstrucción del frlujo venoso en las piernas generando congestión
                           y con ello las manifestaciones clínicas de la trombosis venosa profunda como aumento de
                           volumen de la extremidad (hinchazón), dolor, aumento de la temperatura de la extremidad en
                           comparación con otras partes del cuerpo y cambios de coloración en la piel (habitualmente
                           enrojecimiento). El coágulo puede desprenderse y dirigirse por la circulación venosa hasta el
                           corazón y posteriormente ser bombeado a las arterias pulmonares generando una obstrucción de
                           las mismas.
                        </p>

                        <h1><br>¿Qué factores favorecen que una persona tenga una TEP?</h1>
                        <p>
                           <br>Existen otros factores que pueden aumentar el riesgo de padecer de una tromboembolia
                           pulmonar, entre los cuales se encuentran:
                        </p>

                        <p class="sangria">
                           <br>- Una predisposición individual a la formación de coágulos que se le conoce como
                           trombofilia. Habitualmente los pacientes con este tipo de enfermedades presentan eventos de
                           trombosis recurrentes desde edades tempranas o presentan trombosis en áreas poco frecuentes
                           del cuerpo.
                           <br>- El padecer de cáncer es un factor de riesgo importante. Las células cancerígenas pueden
                           producir algunas sustancias que favorecen la coagulación. .
                           <br>- Los accidentes y las lesiones recientes.
                           <br>- La disminución de la movilidad debida a incapacidad transitoria o permatente. Como ya
                           se ha dicho previamente, la inmobilidad genera una disminución en el flujo sanguíneo que
                           favorece la trombosis.
                           <br>- El uso de tratamientos hormonales. Este factor es el más importante en pacientes
                           mujeres jóvenes. Es por ello que el uso de tratamientos de esta clase deberán ser prescritos
                           y vigilados por un médico,
                           <br>- Infecciones de forma reciente y la hospitalización.
                           <br>- Obesidad
                           <br>- Tabaquismo.

                        </p>

                        <h1><br>¿Qué síntomas se presentan en la TEP?</h1>

                        <p>
                           <br>La sintomatología de la TEP suele ser muy variable. Existen personas que cursan con un
                           cuadro totalmente asintomático; hasta personas que fallecen por causa de esta enfermedad. Los
                           síntomas más comúnmente observados suelen ser la disnea (sensación de falta de aire) y el
                           dolor de pecho. Otros síntomas pueden ser la sensación de palpitaciones muy rápido o muy
                           fuerte, la presencia de coloración rojiza en las expectoraciones pulmonares, entre otras.
                        </p>

                        <h1><br>¿Cómo se diagnostica?</h1>
                        <p>
                           <br>El diagnóstico de TEP requiere atención intrahospitalaria urgente debido a que las
                           pruebas diagnósticas que se solicitan dentro de la valoración, deben de ser realizadas de
                           forma oportuna y valoradas por un médico especialista para detectar casos graves que puedan
                           poner en riesgo la vida y que requieran tratamiento agudo.
                        </p>

                        <p>
                           <br>Existen varias pruebas que se pueden solicitar para valorar en el abordaje de esta
                           enfermedad, las cuales se enlistan a continuación:
                        </p>

                        <p class="sangria">
                           <br>- Dímero D (examen sanguíneo): este marcador se utiliza para valorar inicialmente la
                           probabilidad de que los síntomas de un paciente se deban a un evento relacionado con
                           trombosis como una TEP o una TVP. El Dímero D es un fragmento de proteína que se produce
                           cuando un coágulo se encuentra en proceso de disolución. El proceso de disolución de un
                           coágulo habitualmente inicia desde el momento de su formación. Por ello una medición de
                           Dímero D alta es altamente sugestiva de un proceso de trombosis y requiere mayor evaluación
                           para confirmar el diagnóstico.
                           <br>- Ecocardiografía: en el contexto de pacientes con manifestaciones graves, el ultrasonido
                           cardíaco es fundamental para valorar la repercusión de la TEP sobre el corazón en su porción
                           derecha. Cuando existe una repercusión evidenciada por este método, es necesario realizar un
                           tratamiento más agresivo para evitar en la medida de lo posible complicaciones importantes.
                           <br>- Angio-tomografía pulmonar: este estudio es una forma avanzada de rayos X que permite
                           valorar con mayor resolución las arterias pulmonares y el flujo de medio de contraste
                           (fármaco inyectado detectable por el estudio) a través de las mismas. En la TEP se pueden
                           observar defectos de llenado de las arterias pulmonares compatibles con coágulos que
                           obstruyen parcial o totalmente el flujo sanguíneo.
                           <br>- Gammagrafía pulmonar de ventilación/perfusión: se trata de un estudio de Medicina
                           Núclear en el cual se puede valorar la relación que existe entre la ventilación, es decir la
                           respiración e insuflación de diferentes áreas pulmonares con la perfusión, es decir el flujo
                           sanguíneo de las arterias pulmonares y su aportación de sangre a cada área de los pulmones.
                           Es un estudio que se realiza en contadas ocasiones. Suele reservarse a pacientes con
                           contraindicaciones para el uso de contraste intravenoso.
                        </p>

                        <h1><br>¿Cómo se trata?</h1>
                        <p>
                           <br>El tratamiento de la TEP depende en gran medida de su gravedad, es por ello que se
                           requiere la valoración por un equipo médico de especialistas. En casos graves en los que se
                           compromete la vida o la función cardiovascular, es necesario un tratamiento más intenso. El
                           tratamiento intenso de la TEP puede ser a través de la a administración de fármacos
                           fibrinolíticos o de procecimientos quirúrgicos como una trombectomía mecánica. Los fármacos
                           fibrinolíticos tienen como función la disolución de los coágulos, sin embargo, aumentan
                           considerablemente el riesgo de sangrado. Por lo anterior existen muchas contraindicaciones
                           para el uso de los mismos, mismas que tienen que ser cuidadosamente valoradas por el médico
                           previo a su administración. La trombectomía mecánica es un procedimiento quirúrgico a través
                           del cual se extrae el coágulo que se encuentra obstruyendo el flujo sanguíneo. Este último se
                           reserva para pacientes con contraindicaciones para el uso de fibrinolíticos o en pacientes
                           con falla al tratamiento fibrinolítico.
                        </p>

                        <p>
                           <br>En el resto de los pacientes con TEP, quienes no presentan una enfermedad grave, el pilar
                           del tratamiento son los fármacos anticoagulantes. Estos fármacos evitan la formación de
                           coágulos y con ello la progresión de la enfermedad. Estos fármacos también aumentan el riesgo
                           de sangrado, en menos medida que los fibrinolíticos mencionados previamente. Sin embargo
                           también se contraindican en pacientes que por sus características se consideran de alto
                           riesgo de sangrado.
                        </p>

                        <h1><br>¿Cuál es el pronóstico después de haber tenido una TEP?</h1>
                        <p>
                           <br>La TEP es una enfermedad aguda, es decir curable si se trata de forma adecuada y
                           oportuna. Sin embargo, algunos pacientes con TEP pueden presentar complicaciones a corto,
                           mediano y largo plazo. A corto plazo, la TEP puede poner en riesgo la vida a través del
                           deterioro agudo de la función cardiovascular. También, en pacientes con TEP existe un aumento
                           en el riesgo de nuevos eventos de trombosis, por lo cual es común la recurrencia. A largo
                           plazo, algunos pacientes con TEP desarrollan aumento crónico de las presiones en las arterias
                           pulmonares con lo que se desarrolla una enfermedad conocida como hipertensión pulmonar por
                           tromboembolismo crónico. Esta complicación es poco común, sin embargo genera un deterioro
                           importante en la calidad de vida de las personas caracterizado por disnea progresiva y
                           enfermedad cardíaca, ambos limitando las actividades de la persona que la padece.
                        </p>
                        <p>
                           <br>En caso de haber sufrido una tromboembolia pulmonar, es necesario continuar en
                           seguimiento médico debido a que el tratamiento suele ser prolongado y la recurrencia es alta.
                        </p>

                        <p>
                           <br>Prevención:
                        </p>

                        <p>
                           <br>En situaciones en las que se sabe de antemano que habrá una disminución en la movilidad
                           (como previo a un vuelo, o durante de una jornada de oficina) es recomendable implementar
                           algunas medidas protectoras de la formación de coágulos en las extremidades inferiores:
                        </p>

                        <p class="sangria">
                           <br>• Mover frecuentemente las piernas y los pies, y cambiar constantemente de posición al
                           estar sentado
                           <br>• Levantarse a caminar un poco cada 1 a 2 horas
                           <br>• Considerar el uso de medias de compresión
                           <br>• Abstenerse de fumar justo antes de algún viaje
                        </p>

                        <p>
                           <br>Recuerde lo valioso que es su corazón y su vida, cuide su salud para prevenir una TEP.
                        </p>

