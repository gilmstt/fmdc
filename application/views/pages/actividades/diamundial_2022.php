            <h4><br>DÍA MUNDIAL DEL CORAZÓN</h4>
            <p>
            <br>Todos los años, la Federación Mundial del Corazón y la Organización Mundial de la Salud proclama el 29 de septiembre como el día mundial del corazón. Este día se originó el 29 de septiembre del 2000 cuando la UNESCO y la OMS decidieron establecer una estrategia para concientizar al mundo acerca de las enfermedades cardiovasculares, ya que la frecuencia de estas y la mortalidad comenzaron a aumentar de manera exponencial debido a los nuevos hábitos de vida de la población.</p>
            <br><p>Cada año, se realiza un enfoque diferente para realizar un impacto en la conciencia de las personas para así, poco a poco, disminuir la incidencia y la mortalidad de las enfermedades cardiovasculares. Para realizar este enfoque, la OMS y la OPS utilizan un lema el cual se utiliza en todas las actividades relacionadas con el día mundial. La clase de actividades que se realizan son mesas informativas, charlas, carreras y jornadas de prevención cardiovascular.
            </p><br>

            <h2>LEMAS DEL DÍA MUNDIAL DEL CORAZÓN</h2>
            <p>Un lema es una frase que proyecta un ideal. Es por esto que el día mundial del corazón utiliza un lema diferente cada año para así poder generar una conciencia determinada en la población.</p>
            <p>Si observamos los diferentes lemas a lo largo de la historia del día mundial del corazón, podemos darnos cuenta que el objetivo principal siempre ha sido la prevención a través del impacto de un estilo de vida saludable basado en una adecuada alimentación y actividad física.</p>
            <br><p>Los últimos 5 años se ha tratado de empoderar a la población para que tomemos una actitud de responsabilidad y de capacidad de tomar el control respecto a nuestro estilo de vida para poder evitar las enfermedades cardiovasculares. Incluso, los últimos dos años se han centrado esfuerzos en generar una conciencia social, en donde nos invitan a compartir con los demás nuestro conocimiento y capacidades para que todos podamos tener acceso a una vida saludable.</p>
            <p>
            Este año, las enfermedades cardiovasculares (ECV) siguen siendo la principal causa de muerte en el mundo, lo que provoca 18,6 millones de muertes al año. Tiene muchas causas: desde el tabaquismo, la diabetes, la presión arterial alta y la obesidad, hasta la contaminación del aire y afecciones menos comunes como la enfermedad de Chagas y la amiloidosis cardíaca.
            Para los 520 millones de personas que viven con ECV, COVID-19 ha sido desgarrador. Han tenido un mayor riesgo de desarrollar formas graves del virus. Y muchos han tenido miedo de asistir a citas de rutina y de emergencia, y se han aislado de amigos y familiares.</p>
            <p><br>
            Este año, en el Día Mundial del Corazón, le pedimos al mundo que:
            <br>UTILIZA EL CORAZÓN PARA CONECTARTE 

            <br><br>Aprovechar el poder de la salud digital para mejorar la conciencia, la prevención y el manejo de las enfermedades cardiovasculares a nivel mundial es nuestro objetivo para el Día Mundial del Corazón 2021. La telesalud tiene un papel muy importante que desempeñar.
            <br>UTILIZA EL CORAZÓN PARA CONECTARTE. se trata de usar su conocimiento, compasión e influencia para asegurarse de que usted, sus seres queridos y las comunidades de las que forma parte tengan la mejor oportunidad de vivir una vida saludable para el corazón. Se trata de conectarnos con nuestros propios corazones, asegurarnos de alimentarlos y nutrirlos lo mejor que podamos y de usar el poder de lo digital para conectar cada corazón, en todas partes.
            </p><br>
