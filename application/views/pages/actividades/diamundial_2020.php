               <h1> <br>ENTORNOS SALUDABLES</h1>
               <br><br>El Día Mundial del Corazón hace énfasis este año en la creación de entornos saludables, como estrategia necesaria para reducir los factores de riesgo asociados a las enfermedades cardiovasculares, ya que son la primera causa de muerte en el mundo y en la mayoría de los países de la Región de las Américas, donde causan 1.6 millones de muertes al año, el 30% de ellas prematuras en personas de 30 a 69 años.

               <br><br>Gran parte de estas muertes podría evitarse con una alimentación saludable recudiendo el consumo de sal y grasas saturadas, con actividad física y evitando el consumo de tabaco. Promover y facilitar estos estilos de vida saludable requiere la existencia condiciones que
               <br><br>faciliten el acceso a alimentos frescos y naturales, entornos seguros para la actividad física y donde se promueva una vida libre de humo de tabaco.

               <br><br>La Organización Panamericana de la Salud reconoce la importancia de trabajar impulsando políticas efectivas para reducir la ingesta de sal, fomentar la actividad física y continuar avanzando en la prevención y control del tabaquismo. Estas son algunas de las medidas que promueve el Plan de Acción regional para la prevención y control de las enfermedades no transmisibles.

               <br><br>Reducir la mortalidad por enfermedades cardiovasculares resulta de vital importancia para lograr reducir en un 25% la mortalidad prematura por enfermedades no transmisibles para el año 2025, según el compromiso adquirido en el Plan Mundial de Prevención y Control de Enfermedades No Transmisibles. Como parte de este esfuerzo, la OPS/OMS ha desarrollado un curso virtual para los profesionales de la salud para mejorar el control y prevención de la hipertensión desde la atención primaria y una aplicación para dispositivos móviles que ayuda a estimar el riesgo cardiovascular.

