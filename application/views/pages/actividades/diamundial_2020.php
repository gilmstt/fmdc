            <h2>DÍA MUNDIAL DEL CORAZÓN</h2>
            <p>
            <br>Todos los años, la Federación Mundial del Corazón y la Organización Mundial de la Salud proclama el 29 de septiembre como el día mundial del corazón. Este día se originó el 29 de septiembre del 2000 cuando la UNESCO y la OMS decidieron establecer una estrategia para concientizar al mundo acerca de las enfermedades cardiovasculares, ya que la frecuencia de estas y la mortalidad comenzaron a aumentar de manera exponencial debido a los nuevos hábitos de vida de la población.

            <br><br>Cada año, se realiza un enfoque diferente para realizar un impacto en la conciencia de las personas para así, poco a poco, disminuir la incidencia y la mortalidad de las enfermedades cardiovasculares. Para realizar este enfoque, la OMS y la OPS utilizan un lema el cual se utiliza en todas las actividades relacionadas con el día mundial. La clase de actividades que se realizan son mesas informativas, charlas, carreras y jornadas de prevención cardiovascular.

            <br><br>Este año será un año enfocado en la prevención, ya que la obesidad y la diabetes están aumentando cada vez más y se han convertido en la principal causa de enfermedad cardiovascular a nivel mundial.
            <br><img src="<?=base_url('assets/img/actividades/diamundial2020.jpeg')?>" >
            <h1><br>LEMAS DEL DÍA MUNDIAL DEL CORAZÓN</h1>
            <br>Un lema es una frase que proyecta un ideal. Es por esto que el día mundial del corazón utiliza un lema diferente cada año para así poder generar una conciencia determinada en la población.

            <br><br>Si observamos los diferentes lemas a lo largo de la historia del día mundial del corazón, podemos darnos cuenta que el objetivo principal siempre ha sido la prevención a través del impacto de un estilo de vida saludable basado en una adecuada alimentación y actividad física.
            <br><br>Los últimos 5 años se ha tratado de empoderar a la población para que tomemos una actitud de responsabilidad y de capacidad de tomar el control respecto a nuestro estilo de vida para poder evitar las enfermedades cardiovasculares. Incluso, los últimos dos años se han centrado esfuerzos en generar una conciencia social, en donde nos invitan a compartir con los demás nuestro conocimiento y capacidades para que todos podamos tener acceso a una vida saludable.

            <br><br>Este año, la idea es iniciar con la elaboración de espacios saludables que fomenten y faciliten llevar un estilo de vida saludable. Muy probablemente esto se deba en gran medida a la situación de pandemia que pasamos, en donde nos hemos visto obligados a mantenernos dentro de casa. ¿Es tu casa un ambiente saludable?                
