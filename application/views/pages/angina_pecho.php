                        <p>
                           <br>El presente artículo habla respecto a uno de los principales síntomas de enfermedad
                           cardiovascular: la angina de pecho. Conocer sus características es de suma importancia debido
                           a que la enfermedad cardiovascular es la principal causa de muerte y uno de los principales
                           síntomas de esta es la angina.
                        </p>
                        <h1><br>¿Qué es la angina de pecho?</h1>
                        <p>
                           <br>La angina de pecho se define como el dolor, opresión o incomodidad en el pecho que se
                           desencadena más frecuentemente durante la actividad física. Este dolor es atribuíble a un
                           desbalance entre la demanda de oxígeno del corazón con respecto al aporte del mismo; siendo
                           mayor la demanda que el aporte. La angina de pecho es un factor de riesgo importante para el
                           desarrollo de un infarto agudo al miocardio. El diagnóstico de angina de pecho requiere una
                           extensa valoración y un tratamiento dirigido a disminuir los factores de riesgo
                           cardiovascular y la probabilidad de un evento cardiovascular mayor.
                        </p>
                        <h1><br>¿Cómo se produce la angina?</h1>
                        <p>
                           <br>Como ya se ha mencionado previamente, la angina se produce cuando existe un desbalance
                           entre la demanada y el aporte de oxígeno al corazón.
                        </p>
                        <P>
                           <br>Existen algunas situaciones en las que se aumenta la demanda de oxígeno. Las principales
                           se mencionan a continuación:
                        </p>

                        <p class="sangria">
                           <br> 1. Aumento de la frecuencia cardíaca: durante la actividad física se aumenta la
                           velocidad con la que late el corazón (frecuencia cardíaca). Este aumento de la frecuencia,
                           aumenta la necesidad de oxígeno que ocupa el corazón. Cuando existen factores que disminuyen
                           la capacidad del cuerpo de aumentar el aporte de oxígeno, se puede producir la angina de
                           pecho.
                           <br>2. Aumento de la presión arterial: cuando existe un aumento importante de la presión
                           arterial, existe un aumento de la resistencia al bombeo de la sangre por el corazón. Para
                           compensar la resistencia, el corazón necesita aumentar la fuerza con la que se contrae. Este
                           aumento de la resistencia genera un aumento en la demanda del oxígeno que puede sobrepasar el
                           aporte y con ello producir la angina de pecho.
                        </p>

                        <p>
                           <br>Existen algunas situaciones en las que se disminuye el aporte de oxígeno al corazón. Las
                           principales se mencionan a continuación.
                        </p>

                        <p>
                           <br>1. Oclusión subtotal de las arterias coronarias: la formación de placas de colesterol
                           dentro de las arterias que nutren al corazón, también conocida como enfermedad coronaria,
                           puede generar una incapacidad para el aumento del flujo de sangre en el corazón en
                           situaciones en las que se aumenta la demanda de oxígeno.
                           <br>2. Anemia: la disminución de la hemoglobina, proteína que transporta el oxígeno en la
                           sangre, se conoce como anemia. La anemia es un factor con el cual se disminuye la cantidad de
                           oxígeno que puede llegar al corazón. Sin embargo, habitualmente se necesitan cambios súbitos
                           en la concentración de hemoglobina o una disminución importante para que se produzca angina.
                        </p>

                        <h1><br>¿Cuáles son los síntomas de la angina de pecho?</h1>

                        <p>
                           Los principales síntomas de la angina de pecho son el dolor en el pecho (o molestia) por lo
                           general descrito como opresión que puede irradiarse (moverse) a ambos brazos, cuello,
                           mandíbula, espalda y abdomen, que se desencadena con el esfuerzo físico o algún estrés
                           emocional y que se alivia con el reposo. Algunas veces se puede acompañar de falta de aire
                           (más frecuente), náusea, sudoración y mareo.
                           <br><br>Clásicamente la angina de pecho se quita con el reposo, teniendo una duración
                           aproximada de 5 minutos (hasta 20-25 minutos).

                        </p>

                        <h1><br>¿Cómo se realiza el diagnóstico?</h1>
                        <p>
                           Habitualmente, se limita a los síntomas y la historia clínica del paciente. Por lo general no
                           presenta cambios en los estudios de laboratorio o en el electrocardiograma.
                        </p>
                        <p>
                           Al realizar el diagnóstico, es necesario que el médico estime el riesgo cardiovascular con
                           base en las características del paciente para determinar los estudios de imagen y de la
                           función cardíaca necesarios para confirmar el diagnóstico y determinar las medidas
                           terapéuticas a realizar con el fin de disminuir la probabilidad de progresión a un evento
                           cardiovascular mayor.
                        </p>
                        <h1><br>¿Cómo se trata la angina de pecho?</h1>
                        <p>
                           Todo paciente con angina de pecho debe de ser valorado por un médico para que se realicen los
                           estudios necesarios para determinar qué medidas terapéuticas son pertinentes.
                        </p>
                        <p>
                           El tratamiento con medicamentos al inicio habitualmente se limita a tratamiento sintomático.
                           En personas con riesgo de infarto o muerte por causas cardiovasculares, se puede realizar un
                           cateterismo cardiaco terapéutico con el fin de dismiuir el riesgo y mejorar la angina.
                        </p>
                        <p>
                           Todo paciente con angina de pecho debe de recibir recomendaciones de cambios en el estilo de
                           vida y control de factores de riesgo cardiovascular como control adecuado de la diabetes y la
                           hipertensión arterial, tratamiento para el aumento de los niveles de colesterol, dieta
                           balanceada, ejercicio al menos 30 minutos al día por lo menos 5 días de la semana; así como
                           cese de tabaquismo y disminución del peso.
                        </p>

                        <h1><br>¿Qué pronóstico tiene un individuo con angina de pecho?</h1>
                        <p>
                           Aunque la angina de pecho no deja secuelas permanentes en el corazón, se ha demostrado que
                           eventos de angina predisponen a un evento mayor como un infarto; además, disminuyen la
                           calidad de vida. Por ello, es preciso acudir con un médico para recibir una valoración
                           integral y un tratamiento adecuado.
                        </p>

