                        <p>
                        <br>Sabemos lo difícil que es cambiar un hábito, aún más cuando se trata de algo tan común como la alimentación. No es sencillo lidiar con los antojos, especialmente cuando tenemos hambre y no sabemos que alimento “sano” podemos comer. A continuación, encontrarás consejos útiles para mejorar tu alimentación.

<h1><br> Entender el proceso… </h1>
Lo primero que debemos entender es que una alimentación saludable no implica hacer dietas, ni pasar hambre durante el día, tampoco significa comer ensaladas todo el tiempo, ni renunciar a toda la comida que nos gusta.

<br>Debemos comenzar por hacer pequeños cambios en nuestros malos hábitos de alimentación, incluye a tu familia o a tu núcleo cercano en los cambios positivos, ellos son tu mejor apoyo y obtendrán grandes beneficios al mejorar su alimentación.

<h1><br>Calorías, el numerito en todos los empaques</h1>
Consume las calorías que tu cuerpo necesite, ésta es la clave. Si tus actividades diarias implican pasar la mayor parte de tu día sentado o sin actividad física entonces tu consumo de calorías debe ser menor. Por el contrario, si tus actividades diarias implican ejercicio físico constante tu consumo de calorías debe ser mayor.

<br>No consumas más calorías de las que vas a “gastar” o utilizar en el día. Puedes aumentar la duración e intensidad de tu ejercicio diario para quemar o utilizar calorías; mientras más ejercicio realices más calorías usarás.
<br>Recuerda que todos los empaques muestran información sobre el contenido calórico, de esta forma podrás llevar un control de las calorías ingeridas.

<br>Las calorías que necesitamos dependen de nuestro estilo de vida, edad, género y actividades, por lo que las necesidades dependen de cada persona. Conoce cuantas calorías debes de consumir al día, tu médico o nutriólogo pueden ayudarte. Acude siempre con alguien que tenga estudios profesionales y no te dejes engañar.

<h1><br>¿Qué incluir en mi alimentación?</h1>
<br>Una alimentación saludable no implica comer lo mismo siempre, incluye los diferentes grupos alimenticios en tu dieta diaria; existe una gran variedad de frutas y verduras para elegir procura comer de 4 a 5 porciones al día.
<br>Cereales integrales, pasta, pan y arroz integral son excelentes opciones a incluir en tu dieta. Incluye alimentos como avena y nueces, también productos lácteos bajos en grasa, así como pescado o pollo sin piel ni grasa, puedes consumir carne roja, pero en menor medida procurando que sea carne magra, es decir sin grasa.
<br>Utiliza aceite de olivo, girasol, maíz, canola, soya o cártamo, pero no para freír tus alimentos.
<br>Consume aproximadamente 2 litros de agua al día.
<br>Recuerda que una alimentación saludable comienza con buenas elecciones al momento de comprar alimentos en el supermercado, de esta forma llenarás tu alacena con alimentos que mejoren tu salud y tendrás opciones saludables a la mano.

<h1><br>¿Qué debo alimentos debo limitar?</h1>
<br>Alimentos con grasas saturadas o grasas trans, evita la mantequilla y margarina, descarta los alimentos fritos y nunca reutilices el aceite.
<br>Limita al mínimo alimentos con alto contenido calórico, dulces, refrescos y bebidas azucaradas. Limita también tu consumo de carnes rojas grasas y embutidos.
<br>Descarta comidas con alto contenido de sodio. Finalmente limita tu consumo de alcohol.

<h1><br>Tips</h1>
<br>Cuida el tamaño de las porciones de tus alimentos, mucha comida no implica comer mejor.
<br>Opta por comidas nutritivas, procure que la mitad de su plato sea de frutas y verduras.
<br>Toma nota de todo lo que comes, así sabrás realmente cuánto estás comiendo al día y la cantidad de calorías ingeridas.
<br>Mejora tus decisiones alimenticias, por ejemplo, si te da hambre entre comidas opta por frutas o verduras.
<br>Se realista con los cambios, en ocasiones es mejor hacer cambios pequeños, cambios drásticos pueden desmotivarnos y evitar que mejoremos.
<br>Lee siempre la información nutricional de los empaques, ahí encontrarás las porciones por empaque y la cantidad de calorías por porción. Busca y compara la cantidad de sodio, grasas y azúcares, así como la cantidad de vitaminas y minerales que aporta cada alimento.
<br>Realiza ejercicio o actividad física, llevar un estilo de vida saludable es un proceso integral.

