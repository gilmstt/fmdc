                            <br>Ser sedentario o tener una vida sedentaria, todos hemos escuchado estos términos, pero ¿A qué se refieren? ¿Qué implica ser sedentario? A continuación, todo sobre sedentarismo.

                        <h1><br>¿Qué es?</h1>
                        Sedentarismo se refiere a un estilo de vida caracterizado por la inactividad física, es decir, la falta de ejercicio. El pasar la mayor parte del día sentado sin realizar ningún tipo de actividad física nos hace ser sedentarios.
                        <p>
                            <br>Al analizarlo con detenimiento, una gran cantidad de personas son sedentarias. Esto se debe a que social y culturalmente contamos con un estilo de vida que propicia el sedentarismo. Cada vez caminamos menos para transportarnos ya sea por el uso del carro o transporte público. En muchos trabajos de la actualidad se pasa la mayor parte del tiempo sentado detrás de un escritorio. La mayoría de las ocasiones preferimos no tener que subir escaleras, sino que utilizamos el elevador o las escaleras eléctricas. Mucha genete no hace ejercicio. En los tiempos libres mcuhas veces nos quedamos sentados en el sillón o en la cama viendo televisión o usando la computadora, difícilmente practicamos algún deporte o actividad física.
                        </p>    

                        <h1><br>¿Por qué es malo el sedentarismo?</h1>
                        El sedentarismo se le considera un factor de riesgo para padecer enfermedades cardiovasculares. Una persona sedentaria tiene más probabilidades de sufrir de un infarto cardiaco, enfermedad coronaria o hipertensión. También es uno de los principales factores de riesgo cardiovascular. La falta de actividad física propicia que los  niveles de colesterol en sangre aumenten, favorece el desarrollo del sobrepeso y la  obesidad, y aumenta la incidencia de enfermedades metabólicas como la diabetes. Por todo lo anterior es que el sedentarismo se considera dañino para la salud. No solamente afecta la salud aumentando el riesgo de enfermedades, sino que también se asocia a mayor mortalidad de origen cardiovascular cardiovascular.

                        <h1><br>¿Cómo combatir el sedentarismo?</h1>
                        Como es lógico, el sedentarismo se combate con la actividad física y el ejercicio. Lo recomendable de ejercicio es hacer al menos 30 minutos de ejercicio aérobico de moderada intensidad, al menos 150minutos a la semana, es decir 30min por día, durante 5 días de la semana. 
                        <br>Beneficios del ejercicio:
                        <p class="sangria">
                            <br>1.	Fortalece nuestro corazón y sistema circulatorio, haciéndolos más resistentes a la disminución en el aporte de oxígeno, como sucede en el infarto agudo al miocardio. 
                            <br>2.	Ayuda a mantener un peso adecuado y con ello disminuye la incidencia y morbilidad de las enfermedades metabólicas como la diabetes. 
                            <br>3.	Ayuda a mantener nuestros huesos y músculos fuertes y resistentes. 
                            <br>4.	Disminuye los niveles de estrés, nos mantiene activos y saludables y eleva el  autoestima.
                        </p>
                        <p>
                            <br>Sabemos que no es fácil cambiar un hábito, pero vale la pena mejorar. Si dejamos atrás el sedentarismo aumentaremos nuestro tiempo y calidad de vida. Comenzar el cambio es lo importante, se puede empezar poco a poco, no tiene que ser un cambio radical. 
                            <br>Pequeñas acciones como usar las escaleras, ir al trabajo en bici, dejar el carro estacionado más lejos o bajar del autobús unas cuadras antes, salir a caminar por las tardes, hacer jardinería o el aseo del hogar son buenas alternativas.
                            <br>Incorporar ejercicio o una actividad física no tiene que ser algo desagradable, existen muchas opciones. Podemos elegir entre caminar, correr o andar en bicicleta al aire libre o bien en el gimnasio; tomar clases de baile, zumba o aeróbicos, nadar, hacer yoga, pilates, tai chi o artes marciales, salir a patinar, jugar tenis, fútbol, voleibol o básquetbol, hay una amplia gama de actividades elija de acuerdo a sus intereses y disfrute el ejercicio, lo importante es moverse y mantenerse activos.
                            <br><br>Si comparamos los beneficios que tiene hacer ejercicio contra los riesgos y problemas a la salud por ser sedentario nos podemos dar cuenta que mantenernos activos día a día es algo muy simple, pequeñas acciones que traen grandes recompensas.
                            <br>Mayor tiempo de vida y mejor calidad en ella.
                            <br>Cambia tu estilo de vida hoy, deja el sedentarismo atrás y disfruta de todos los beneficios en tu salud y en tu vida.
