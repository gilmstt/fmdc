                            <br>Ser sedentario o tener una vida sedentaria, todos hemos escuchado estos términos, pero ¿A qué se refieren? ¿Qué implica ser sedentario? A continuación, todo sobre sedentarismo.

<h1><br>¿Qué es?</h1>
Sedentarismo se refiere a un estilo de vida caracterizado por la falta de ejercicio o inactividad física. Pasar la mayor parte del día sentado sin actividad física es ser sedentario.

<br><br>Si lo analizamos con detenimiento, muchas personas son sedentarias, esto se debe a que social y culturalmente contamos con comodidades que propician el sedentarismo; caminamos menos para transportarnos ya sea por el uso del carro o transporte público, en el trabajo pasamos la mayor parte del tiempo sentados detrás de un escritorio, no subimos escaleras al preferir el ascensor o escaleras eléctricas, llegamos cansados o tarde a la casa y no hacemos ejercicio, nos quedamos en el sofá o en la cama para ver la televisión o usar la computadora y difícilmente practicamos algún deporte o actividad física.

<h1><br>¿Qué hay de malo en esto?</h1>
El sedentarismo genera problemas en la salud, se le considera un factor de riesgo para padecer enfermedades del corazón. Una persona sedentaria tiene más probabilidades de infarto cardiaco, enfermedad coronaria o hipertensión. La falta de actividad física hace que los niveles de colesterol malo aumenten, propicia la obesidad, sobrepeso y enfermedades metabólicas. El sedentarismo es dañino para la salud y se asocia a mayor mortalidad cardiovascular, lo bueno es que se puede combatir y prevenir.

<h1><br>¿Cómo evitar o tratar el sedentarismo?</h1>
La inactividad o sedentarismo se combate con ejercicio, ya que fortalece nuestro corazón y sistema circulatorio. También nos ayuda a mantener un peso adecuado; el ejercicio ayuda a mantener nuestros huesos y músculos fuertes, nos mantiene activos y saludables, eleva nuestro autoestima y disminuye los niveles de estrés.

<br><br>Sabemos que no es fácil cambiar un hábito, pero vale la pena mejorar. Si dejamos atrás el sedentarismo aumentaremos nuestro tiempo y calidad de vida. Comenzar el cambio es lo importante, se puede empezar poco a poco, no tiene que ser un cambio radical. Pequeñas acciones como usar las escaleras, ir al trabajo en bici, dejar el carro estacionado más lejos o bajar del autobús unas cuadras antes, salir a caminar por las tardes, hacer jardinería o el aseo del hogar son buenas alternativas.
Incorporar ejercicio o una actividad física no tiene que ser algo desagradable, existen muchas opciones. Podemos elegir entre caminar, correr o andar en bicicleta al aire libre o bien en el gimnasio; tomar clases de baile, zumba o aeróbicos, nadar, hacer yoga, pilates, tai chi o artes marciales, salir a patinar, jugar tenis, fútbol, voleibol o básquetbol, hay una amplia gama de actividades elija de acuerdo a sus intereses y disfrute el ejercicio, lo importante es moverse y mantenerse activos.

<br><br>Si comparamos los beneficios que tiene hacer ejercicio contra los riesgos y problemas a la salud por ser sedentario nos podemos dar cuenta que mantenernos activos día a día es algo muy simple, pequeñas acciones que traen grandes recompensas.
<br>Mayor tiempo de vida y mejor calidad en ella.
<br>Cambia tu estilo de vida hoy, deja el sedentarismo atrás y disfruta de todos los beneficios en tu salud y en tu vida.

