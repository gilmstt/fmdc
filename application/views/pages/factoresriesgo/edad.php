                        <p>
                            <br>¿Porqué la edad nos afecta? ¿Qué sucede con nuestro corazón a mayor edad?¿Qué puedo hacer para mejorar? A continuación, todo sobre la edad y corazón.

                            <br><br>Con el paso del tiempo es normal que nuestro cuerpo experimente cambios asociados a la edad; envejecer no es malo, es un proceso natural que se da con los años.
                            <br>Sin embargo, a mayor edad existe mayor riesgo de enfermedad cardiaca, esto se debe a que con el tiempo las arterias se endurecen y pierden flexibilidad, el corazón se desgasta, con el paso de los años le cuesta más trabajo bombear sangre al organismo y puede aumentar su tamaño. Por ello el riesgo de infarto cardiaco, hipertensión y enfermedad coronaria aumenta con la edad.

                            <br><br>Debemos entender que esto es un proceso normal, pensemos cuando éramos niños, podíamos correr, saltar, caer, levantarnos y aun así seguir jugando por horas sin cansarnos; al crecer no tenemos la energía para correr y jugar todo el día, nos cansamos con cosas que antes solíamos hacer. Lo mismo pasa con nuestro corazón, con el paso de los años le cuesta más trabajo hacer lo que hacía antes sin cansarse.

                            <br><br>Sabemos que no podemos evitar el paso del tiempo ni los riesgos que la edad genera en nuestra salud, pero sí podemos cambiar otros factores de riesgo al llevar un estilo de vida saludable, así cuidaremos nuestro corazón oportunamente y reduciremos las probabilidades de enfermar.

                            <br><br>No fumar o dejar de fumar, hacer ejercicio y levar una alimentación cardiosaludable son excelentes formas de prevenir. Cuidemos nuestro peso, niveles de colesterol, presión y glucosa. Tomemos acciones ahora y nuestra salud será mejor mañana.

                            <br><br>Cuando entendemos el gran trabajo que realiza nuestro corazón y que eventualmente va a envejecer con nosotros, podemos ser conscientes y cuidarlo para que se mantenga lo más sano posible.

                            <br><br>Un corazón viejo no tiene que ser un corazón enfermo, entendamos que, a pesar de tener mayor riesgo de enfermar, puede seguir siendo un corazón sano y funcional si lo cuidamos adecuadamente con un estilo de vida saludable.

