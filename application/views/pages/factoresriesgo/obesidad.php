                       <p>
                        <br>Obesidad, escuchamos éste término constantemente, en los medios de comunicación, en el médico e incluso en la familia, pero ¿Sabemos realmente cuál es el problema de tener varios kilos de más? ¿Cómo afecta la salud de nuestro corazón? A continuación, encontrarás información importante sobre la obesidad:
<br>La obesidad es un problema de salud que pone en riesgo nuestra vida, conocer las consecuencias que trae consigo nos ayuda a ser conscientes y tomar acciones para mejorar nuestro estilo de vida y estar sanos.
</p>
<h1>
<br>¿Qué es la obesidad?
</h1>
<p>
Todos los seres humanos tenemos cierto porcentaje de agua, de músculo y de grasa, cuando hay un desbalance y el porcentaje de grasa aumenta se le llama obesidad.

<br>Pero, ¿por qué es “mala”? Una persona con obesidad tiene depósitos de grasa o tejido adiposo excesivos para el cuerpo, esto genera daños en el organismo y aumenta el trabajo que tiene que hacer el corazón para llevar sangre a todo el cuerpo.
<br>La obesidad puede causar enfermedades cardiacas, aumenta el riesgo de padecer un infarto al corazón, de sufrir una hemorragia o infarto cerebral, las personas con obesidad tienen mayor riesgo de desarrollar diabetes o hipertensión, hace que los niveles de colesterol malo (LDL) y de triglicéridos aumenten en la sangre y que el colesterol bueno (HDL) disminuya.
Por ello la obesidad es un factor de riesgo cardiovascular muy importante, pero prevenible si hacemos cambios saludables en el estilo de vida.
</p>
<h1>
<br>¿Por qué sucede? ¿Qué causa la obesidad?                        
</h1>
<p>
La obesidad se presenta cuando comemos más de lo que gastamos, es decir cuando la ingesta de alimentos o de calorías es mayor a lo que usamos en la vida diaria.
<br>Por ejemplo, si hacemos mucho ejercicio o actividad física diariamente nuestro cuerpo necesitará más calorías, ya que el trabajo es mayor; pero si hacemos poco ejercicio en la vida cotidiana o si estamos sentados la mayor parte de día necesitamos menos calorías. Debemos comer de acuerdo a nuestras actividades; si comemos muchas calorías y no las usamos, nuestro cuerpo las acumulará en forma de grasa.
</p>
<h1>
<br>¿Cómo identificar la obesidad?
</h1>
<p>
Para calcular la grasa acumulada en el cuerpo se utiliza el perímetro abdominal. Para medirlo la persona debe estar de pie, se coloca una cinta métrica alrededor del abdomen a la altura del ombligo y se mide la circunferencia. Una medición normal en mujeres debe ser menor a 80 cm mientras que en hombres lo normal es menor a 90 cm. Cuando estas mediciones son más altas, el riesgo cardiovascular aumenta.

<br>La obesidad también se puede medir utilizando el Índice de Masa Corporal conocido por sus siglas como IMC. Para calcularlo se utiliza la fórmula IMC=kg/m2, es decir, peso sobre estatura al cuadrado. El IMC puede indicar peso bajo, peso normal, sobrepeso y obesidad, de acuerdo a los resultados.
<br>- Peso bajo o peso inferior al normal: IMC menor a 18.4
<br>- Peso normal: IMC de 18.5 a 24.9
<br>- Sobrepeso: 25 a 29.9
<br>- Obesidad: Mayor a 30

<br>El IMC y el perímetro abdominal son excelentes herramientas para lograr nuestro peso ideal; los podemos usar para monitorizar nuestro progreso e imponer metas de peso a corto y largo plazo.
</p>
<h1>
<br>¿Cuáles son las complicaciones o daños a la salud?
</h1>
<p>


La obesidad puede generar los siguientes daños a la salud:

<br>• Aumenta el riesgo de enfermedades cardiacas y de infarto al corazón
<br>• Mayor riesgo de hemorragia o infarto cerebral
<br>• Riesgo de desarrollar hipertensión
<br>• Riesgo de desarrollar diabetes y enfermedades metabólicas
<br>• Aumento en los niveles de colesterol malo (LDL) y triglicéridos
<br>• Disminuye los niveles de colesterol bueno (HDL)
<br>• Aumenta el riesgo desarrollar apnea obstructiva del sueño y problemas respiratorios
<br>• Desarrollo de cálculos biliares
<br>• Osteoartritis, por daño a los huesos y articulaciones
<br>• Dolor muscular
<br>• Depresión y ansiedad
<br>• Mayor riesgo de cáncer
<br>• Aumento en la mortalidad asociada a obesidad
<br>• Baja calidad de vida

<br><br>La obesidad impacta negativamente en la calidad de vida, toma acción y evita los riesgos que este padecimiento genera.
</p>
<h1>
<br>Prevención y Tratamiento
</h1>
La obesidad se puede prevenir y tratar realizando ejercicio o actividad física y cuidando la alimentación.
<br>Llevar una dieta cardiosaludable con un balance adecuado entre la energía o calorías que ingerimos y las que utilizamos en nuestra vida cotidiana, es vital.
<br>Resulta muy útil plantear metas a corto plazo, ya que nos ayuda a lograr el objetivo deseado y a continuar con la motivación necesaria para seguir y lograr nuestro peso ideal.
<br>En ocasiones hacer un cambio de alimentación no es sencillo, por lo que es importante solicitar ayuda nutricional, el apoyo y los conocimientos de un profesional en nutrición son invaluables.
<br>Pregunte o solicite a su médico por una consulta con un licenciado en nutrición.
<br>Algunas personas pueden requerir el uso de medicamentos, recuerde que esta decisión depende sólo de su médico ya que existen efectos secundarios relacionados a su uso. No utilice “productos milagro” puede causar efectos negativos sobre su salud y su metabolismo. Recuerde buscar siempre apoyo y ayuda profesional.
<br>Cuide su salud, su corazón y su vida, recuerde que haciendo cambios positivos en nuestros hábitos y estilo de vida tendremos grandes beneficios.
