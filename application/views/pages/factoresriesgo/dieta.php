                                <h1><br>Grasas Dislipidemias</h1>
                            <p>    
                                <br><br>Las grasas o lípidos (su nombre científico) forman parte de los macronutrientes. Estas son esenciales para nuestro organismo ya que forman las hormonas y ayudan a nuestras células a permanecer estables.

                                <br><br>Existen básicamente 3 grasas que tienen utilidad clínica: colesterol LDL (malo), colesterol HDL (bueno) y triglicéridos. También existen los ácidos grasos trans (malos) y cis (buenos). El exceso de los lípidos malos incrementa el riesgo de padecer enfermedad coronaria y cerebrovascular (embolias).

                                <br><br>Hay que tener un equilibrio, ya que tener muy pocas grasas en nuestro cuerpo por hacer dietas excesivas nos puede llevar a desnutrición y problemas hormonales. Una dieta balanceada basada en el plato del buen comer es la clave para llevar una vida saludable.

                                <br><br>Te recomendamos que las grasas ocupen un poco menos de la cuarta parte de tu plato.

                                <br><br>Es muy importante acudir con tu nutriólogo al menos 2 veces al año.
                            </p>
                            <h1><br>Carbohidratos</h1>
                            <p>
                                <br>Los carbohidratos forman parte de los macronutrientes. Son esenciales para darnos la energía que necesitamos a lo largo del día. Puedes encontrarlos en alimentos como el pan, el arroz o tortilla.

                                <br><br>Existen alimentos que nos dan carbohidratos de mala calidad como los pasteles, dulces o comida chatarra, con muy pobre valor nutricional.

                                <br><br>El comer carbohidratos en exceso provoca sobrepeso y obesidad, aumentando el riesgo de diabetes, presión alta y enfermedades cardiovasculares. El comer carbohidratos en muy poca cantidad, puede llevarnos a la desnutrición e hipoglucemia (azúcar baja) lo cual es muy peligroso.

                                <br><br>Para tener una dieta saludable es necesario utilizar el plato del buen comer. Los carbohidratos deben ocupar un cuarto de tu plato de comida y debes procurar comer carbohidratos saludables como pan integral, centeno, etc.

                                <br><br>Es muy importante acudir con tu nutriólogo al menos 2 veces al año.
            
