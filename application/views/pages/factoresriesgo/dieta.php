                                <br><br>Una de los factores para evitar la obesidad es cuidar la dieta, pero ¿qué significa esto?. Dieta del latín «diaeta», «régimen o modo de vida», régimen alimentario es la composición, frecuencia, cantidad y bebidas que constituye la alimentación de los seres vivos conformando hábitos o comportamientos nutricionales.

                                <br><br>Una dieta normal o correcta, es aquella donde una persona consume alimentos de forma balanceada, es decir, en el porcentaje y cantidad adecuada para mantener una vida saludable.

