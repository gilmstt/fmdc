                        <p>
                            <br>Todos hemos escuchado que fumar es malo para la salud, pero, ¿sabemos qué problemas genera? ¿porqué es importante dejarlo? ¿qué tanto daño hace?

<br><br>Cuando una persona fuma, aumenta su frecuencia cardiaca, es decir, hace que los latidos del corazón sean más rápidos. Este efecto produce que el corazón trabaje más de lo necesario y que en ocasiones desarrolle alteraciones en el ritmo. Fumar aumenta los niveles de colesterol malo (LDL) y disminuye el colesterol bueno (HDL). Los efectos crónicos del tabaquismo disminuyen el oxígeno que entra llega a los órganos del cuerpo y aumenta la presión arterial.

<br><br>Debido a todo lo anterior el tabaquismo es un factor de riesgo cardiovascular ya que no sólo predispone a infarto al corazón, sino que genera problemas crónicos y graves a distintos niveles. Se le considera una causa de muerte prevenible.

<br><br>El tabaquismo es una adicción. El cigarrillo contiene muchas sustancias tóxicas que son nocivas para la salud. Por ello, aunque usted fume “poco” o sólo en algunas ocasiones, aumenta su riesgo de enfermedad cardiovascular y la probabilidad de muerte prematura. Tome en cuenta que al fumar, usted daña la salud de las personas que se encuentran a su alrededor. El humo del cigarrillo es perjudicial y múltiples daños a la salud.

<br><br>Fumar no sólo ha demostrado aumentar el riesgo de padecer cáncer. También se le ha relacionado con enfermedades cardiacas e infarto al corazón, daño a los vasos sanguíneos y con eventos trombóticos, es decir con la generación de coágulos dentro de las arterias. Por eso fumar aumenta el riesgo de un infarto cardiaco o cerebral.

<br><br>El tabaquismo también aumenta el riesgo de padecer enfermedades respiratorias, entre ellas, enfermedad pulmonar obstructiva crónica (EPOC), enfisema y desde luego cáncer pulmonar. De igual forma fumar se asocia también a otras variedades de cáncer, de las cuales destacan el cáncer de laringe, boca, garganta, páncreas, vejiga, riñón y cérvix.

<br><br>No disminuya su calidad de vida y la de las personas que lo rodean. Opte por mejorar su salud y prolongar su vida.

<br><br>La mejor solución es dejar de fumar. Aquellas personas que dejan de fumar previenen una muerte temprana.

<br><br>Sabemos que no es sencillo, pero tampoco imposible. La decisión de dejarlo es un buen comienzo.

<br<br>>Busque apoyo de un profesional, quien seguramente estará dispuesto a ayudarle. Platique sobre el tema y pida alternativas para dejar de fumar.

<br><br>Busque un grupo o programa de apoyo. La asesoría y orientación que proveen resultan muy útiles.
