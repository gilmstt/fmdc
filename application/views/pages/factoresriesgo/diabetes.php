                        <h6><span><i class="fas fa-heart"></i></span>Diabetes Mellitus</h6>
                    </div>
                    <div class="row " style="text-align: justify;">
                        <p>
                            Escrito por el Dr. Alejandro López Leija. Medicina Interna.
                        </p>
                        
                        <p>
                            <br>La diabetes mellitus, es una enfermedad en donde los niveles de azúcar o glucosa en sangre se elevan.Esto sucede debido a que la insulina no es suficiente o bien no funciona adecuadamente.
                            <br>La insulina es una hormona que produce el páncreas para permitir que la glucosa obtenida en los alimentos ingrese a las células para producir la energía necesaria para las funciones de todo el organismo y así podamos movernos, pensar y realizar todas las actividades de una vida normal.Cuando se tiene diabetes, la insulina no es suficiente para llevar la glucosa al interior de las células, ya sea porque el páncreas no la produce en cantidad suficiente o porque la que se produce no funciona correctamente; si la glucosa no se transporta a las células sus niveles en sangre se elevan. Los altos niveles de azúcar generan daños graves a la salud.
                        </p>

                        <h1><br>Existen diferentes tipos de diabetes:</h1>
                        
                        <h1 style="width: 100%"><br>diabetes tipo 1</h1>
                            
                        <p>
                            <br>En este tipo de diabetes la producción de insulina se produce en poca cantidad. Se debe al funcionamiento inadecuado de las células productoras de esta hormona. En muchas ocasiones, estos pacientes deben administrarse insulina una o mas veces al día.
                        </p>
                        
                        <h1><br>Diabetes Tipo 2</h1>
                        <P>
                            <br>Este tipo de diabetes es la más común. La razón habitual es que aunque el paciente sea capaz de producir una cantidad normal o incluso mayor de insulina puede existir una resistencia a los efectos de la misma en los tejidos. Aunque es más común en adultos, cada vez se presenta más en jóvenes e incluso niños. Se asocia frecuentemente al sobrepeso.
                            En un inicio este tipo de diabetes puede tratarse con cambios en el estilo de vida, como alimentación saludable y ejercicio.
                        </p>
                        
                        <h1><br>Diabetes Gestacional</h1>
                        <P>
                            <br>Este tipo de diabetes se presenta en algunas mujeres embarazadas y genera riesgos graves para el feto, por lo que es muy importante llevar un buen control de la glucosa durante el embarazo.
                            Las mujeres con diabetes gestacional habitualmente vuelven a tener valores normales de glucosa en sangre después del parto, pero tienen un mayor riesgo de padecer diabetes tipo 2 en un futuro.
                            
                        </p>
                        
                        <p>
                        <br>La diabetes es un factor de riesgo cardiovascular. Cuando esta enfermedad no se trata ni se controla puede generar graves daños a la salud. Los niveles elevados de glucosa o azúcar en sangre aumentan el riesgo de sufrir un infarto al corazón y de otras enfermedades cardiovasculares como insuficiencia cardiaca.
                            La diabetes daña los riñones por lo que genera enfermedad renal y necesidad de diálisis; afecta los nervios en las extremidades ocasionando alteraciones de la sensibilidad, dificulta la cicatrización de las heridas y disminuye la capacidad para combatir infecciones. Asimismo genera obstrucción al flujo sanguíneo en las arterias lo cual puede finalmente provocar amputaciones.
                        </p>
                        
                        <p>
                            <br>Del mismo modo esta enfermedad afecta a los ojos y genera pérdida de la visión o ceguera, así como mayor riesgo de infarto o hemorragia cerebral.
                        </p>
                        
                        <p>
                            <br>La diabetes representa riesgos y complicaciones para la salud. Pero, la buena noticia es que todos se pueden prevenir con un buen control de los niveles de azúcar en sangre.
                        </p>

                        <p>
                            <br>Hay que tomar en cuenta que en muchas ocasiones los síntomas no se perciben y las personas con diabetes no se sienten mal.Pueden pasar años sin ser diagnosticadas hasta presentar graves complicaciones en la salud.

                        </p>

                        <p >
                            <br>Los síntomas que pueden llegar a presentar algunas personas son:
                        </p>

                        <p style="width: 100%">
                        <br >• Aumento de apetito
                        <br>• Sed
                        <br>• Aumento en la necesidad de orinar, en ocasiones por las noches
                        <br>• Cansancio o fatiga
                        <br>• Visión borrosa
                        <br>• Pérdida de peso
                        </p>

                        <p>
                           <br> Si usted presenta estos síntomas acuda al médico, sólo él puede diagnosticar esta enfermedad.

                           <br><br>Recuerde que en muchas personas la diabetes no da síntomas, por lo que es importante conocer sus niveles de glucosa, especialmente si usted tiene factores de riesgo.

                           <br><br>Si usted ya padece diabetes debe tratarse adecuadamente.
                           <br><br>La diabetes tipo 2 es una enfermedad que se puede evitar si realizamos las acciones adecuadas. Adoptar un estilo de vida saludable es crucial para prevenir ésta y otras enfermedades.

                            <br><br>El tratamiento de la diabetes debe ser basado en cambios del estilo de vida. Sin embargo estos cambios pueden no ser suficiente para lograr un control adecuado por lo que su médico, después de realizar una revisión cuidadosa, le prescribirá insulina o diversos medicamentos capaces de controlarla. Es importante aclarar que el uso de insulina no lleva consigo la posibilidad de efectos colaterales ni adelanta las complicaciones crónicas y tardías de una Diabetes mal tratada.
                            <br><br>El monitoreo de los niveles de glucosa en sangre es de vital importancia para llevar un control adecuado, así como la revisión de los niveles de colesterol y presión arterial.
                            <br><br>Ayude a su corazón y cuide su vida, mantenga su peso ideal, realice ejercicio o actividad física, lleve una alimentación balanceada, baja en carbohidratos y grasas saturadas, evite fumar y modere su consumo de alcohol. Estas medidas ayudarán a disminuir la progresión de la enfermedad y evitará las complicaciones que trae consigo.
                        </p>
