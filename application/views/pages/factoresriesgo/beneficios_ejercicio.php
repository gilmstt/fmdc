                            <h1><br>BENEFICIOS DE LA ACTIVIDAD FÍSICA</h1>

                            <br><br>La actividad física es esencial para el mantenimiento y mejora de la salud y la prevención de las enfermedades, para todas las personas y a cualquier edad. La actividad física contribuye a la prolongación de la vida y a mejorar su calidad, a través de beneficios fisiológicos, psicológicos y sociales, que han sido avalados por investigaciones científicas.

                            <h1><br>BENEFICIOS FISIOLÓGICOS</h1>

                            <br><br>La actividad física reduce el riesgo de padecer: enfermedades cardiovasculares, tensión arterial alta, cáncer de colon y diabetes.

                            <br><br>Ayuda a controlar el sobrepeso, la obesidad y el porcentaje de grasa corporal.
                            <br>- Fortalece los huesos, aumentando la densidad ósea.
                            <br>- Fortalece los músculos y mejora la capacidad para hacer esfuerzos sin fatiga (forma física).
                            <br>- Fortalece tu sistema inmunológico
                            <br>- Relajación posterior a un estado de estrés
                            <br>- Ayuda a conciliar el sueño
                            <br>- Aumenta la masa muscular
                            <br>- Disminuye la presión arterial
                            <br>- Reduce el colesterol y triglicéridos

                            <h1 style="width: 100%"><br>BENEFICIOS PSICOLÓGICOS</h1>

                            <br>- La actividad física mejora el estado de ánimo
                            <br>- Disminuye el riesgo de padecer estrés, ansiedad y depresión
                            <br>- Aumenta la autoestima
                            <br>- Proporciona bienestar psicológico.

                            <h1 style="width: 100%"><br>BENEFICIOS SOCIALES</h1>

                            <br>- Fomenta la sociabilidad.
                            <br>- Aumenta la autonomía y la integración social, estos beneficios son especialmente importantes en el caso de discapacitación física o psíquica.

                            

                            <h1><br>BENEFICIOS ADICIONALES EN LA INFANCIA Y ADOLESCENCIA</h1>
                            <p>
                            <br>Los beneficios comprobados en estas etapas son:

                            <br><br>La contribución al desarrollo integral de la persona.
                            <br>- El control del sobrepeso y la obesidad. En esta etapa, el control de la obesidad es muy importante para prevenir la obesidad adulta.
                            <br>- Mayor mineralización de los huesos y disminución del riesgo de padecer osteoporosis en la vida adulta.
                            <br>- Mejor maduración del sistema nervioso motor y aumento de las destrezas motrices.
                            <br>- Mejor rendimiento escolar y sociabilidad.
