                        <p>
                            <br>¿El estrés puede afectar mi corazón? ¿Qué riesgos tiene sufrir estrés? Con tantas presiones en la vida diaria ¿cómo no sentir estrés? A continuación, todo sobre estrés y corazón.

                            <br><br>Todos sentimos estrés, las situaciones de la vida cotidiana no siempre son sencillas y en ocasiones sentimos que los problemas se acumulan. Sentir estrés no es malo, la forma en que reaccionamos al estrés sí y esto puede generar problemas en nuestra salud y nuestro corazón.

                            <br><br>Existen varias razones por las cuales el estrés afecta la salud cardiovascular, aunque los estudios en el tema continúan.
                            <br>Cuando sentimos estrés liberamos distintas sustancias, entre ellas adrenalina, una hormona que aumenta nuestra frecuencia cardiaca, es decir hace que el corazón lata a mayor velocidad y necesite más oxígeno para funcionar. La adrenalina también hace que respiremos más rápido e incluso que nuestra presión arterial aumente.

                            <br><br>El estrés, sobre todo cuando es constante, puede generar dolores de cabeza, cuello o espalda, así como problemas gastrointestinales. También puede ocasionar dificultad para conciliar el sueño o para dormir adecuadamente, genera falta de energía, ansiedad o irritabilidad.

                            <br><br>El manejo inadecuado del estrés contribuye al consumo o aumento en el consumo de tabaco, muchas personas fuman porque sienten que es una forma de liberar el estrés. El tabaquismo genera serios problemas al corazón y a nuestras arterias, aumentando el riesgo de infarto cardiaco.
                            <br>Existe también una asociación entre el estrés y consumo excesivo de alcohol, lo que tiene consecuencias negativas sobre el corazón y la salud.
                            <br>Desafortunadamente el estrés también puede ocasionar un aumento en el consumo de alimentos, comer por estrés genera sobrepeso, obesidad, alteración en los niveles de lípidos y enfermedad cardiaca.

                            <br><br>Debido a lo anterior, el estrés se relaciona a hipertensión, aumento en niveles de colesterol, riesgo de hemorragia o infarto cerebral, enfermedad coronaria y riesgo de infarto cardiaco.

                            <br><br>El manejo adecuado del estrés es muy importante, ya que nos ayuda a mejorar nuestra salud e incluso a disminuir el riesgo de enfermedad cardiovascular si hacemos ejercicio para lidiar con él. Existen diferentes alternativas para manejar el estrés:
                                <br>Salir a correr, ir al gimnasio, dar un paseo o caminata en lugar de fumar un cigarrillo, hacer yoga, pilates, meditación, practicar ejercicios de respiración, escuchar música, pasar tiempo con tu mascota, leer un libro, pintar, dibujar o tocar un instrumento, un paseo en bicicleta, hacer jardinería o ponerte en contacto con la naturaleza, también existen clases para manejo del estrés.

                                <br><br>Debemos tomar en cuenta que no todos lidiamos con el estrés de la misma forma, por lo que no debemos compararnos con los demás. Cada quién es distinto y debemos elegir la alternativa o alternativas que mejor se acoplen a nosotros, de esta forma podremos manejar el estrés adecuadamente.

