                    <div class="row " style="text-align: justify;">
                        <p>
                            Escrito por el Dr. Alejandro López Leija. Medicina Interna.
                        </p>
                        
                        <p>
                            <br>¿El estrés puede afectar mi corazón? ¿Qué riesgos tiene sufrir de mucho estrés? A continuación, todo sobre estrés y corazón.
                            <br><br>Todos sentimos estrés, las situaciones de la vida cotidiana no siempre son sencillas y en ocasiones sentimos que los problemas se acumulan. Sentir estrés no es malo, sin embargo, una gran cantidad de estrés puede generar problemas en la salud y en nuestro corazón.
                            <br><br>Existen varias razones por las cuales el estrés puede afectar nuestra salud y la salud cardiovascular.
                             
                            <p class="sangria">
                                <br>1.	Cuando sentimos estrés liberamos distintas sustancias, entre ellas adrenalina, una hormona que aumenta nuestra frecuencia cardiaca, es decir hace que el corazón lata a una mayor velocidad y necesite de una mayor cantidad de oxígeno para funcionar. La adrenalina también hace que respiremos más rápido e incluso que nuestra presión arterial aumente. El aumento de la presión arterial es uno de los principales factores de riesgo cardiovascular y de otras enfermedades como la enfermedad renal crónica. 
                                <br><br>2.	El estrés, sobre todo cuando es constante, puede generar un aumento de la tensión muscular en todo el cuerpo, favoreciendo la aparición de dolores de cabeza, cuello o espalda. 
                                <br><br>3.	También favorece la aparición de síntomas  gastrointestinales como diarrea o estreñimiento, dolor abdominal, intolerancia a ciertos alimentos, entre otros. 
                                <br><br>4.	El estrés genera alteraciones del sueño también. Se asocia con insomnio, sobre todo aquel en el que existe una mayor dificultad para conciliar el sueño. Lo anteriore puede generar falta de energía, ansiedad o irritabilidad. 
                                <br><br>5.	El manejo inadecuado del estrés contribuye al consumo o aumento en el consumo de tabaco y de otras sustancias. Muchas personas fuman porque sienten que es una forma de liberar el estrés. Como se describe en otro artículo, el tabaquismo genera serios problemas al corazón y a nuestras arterias, aumentando el riesgo de infarto cardiaco y de otras enfermedades cardiovasculares.  Existe también una asociación entre el estrés y consumo excesivo de alcohol. El consumo excesivo de alcohol se asocia con enfermedades metabólicas y del hígado. Estas enfermedades aumentan el riesgo carciovascular y pueden tener consecuencias negativas importantes sobre la salud e incluso poner en riesgo la vida.
                                <br><br>6.	En algunas personas, el estrés se asocia con un aumento en el consumo de  consumo de alimentos. Comer por estrés se asocia con sobrepeso y obesidad, así como, alteración en los niveles de colesterol. Estos últimos se asocian con un aumento importante del riesgo cardiovascular. 
                            </p>   

                            <p>
                                <br>Debido a lo anterior, el estrés se asocia con un aumento en el riesgo cardiovascular. Por ello los altos niveles de estrés se asocian con la enfermedad coronaria, el infarto agudo al miocardio, la enfermedad vascular cerebral, las hemorragias intracraneales, entre otras enfermedades.
                                <br><br>Existen diferentes alternativas para manejar el estrés:
                            </p>
                            
                            <p class="sangria">
                                <br>1.	Hacer ejercicio: salir a correr, ir al gimnasio, dar un paseo o caminata, hacer yoga, pilates. La actividad física es una forma de manejar el estrés, además de que se asocia con una mayor disminución del riesgo cardiovascular. 
                                <br><br>2.	Los ejercicios de relajación como la meditación y los ejercicios de respiración son otra forma de manejar los altos niveles de estrés. 
                                <br><br>3.	Las actividades recreacionales como escuchar música, pasar tiempo con tu mascota, la jardinería,  leer un libro, pintar, dibujar o tocar un instrumento son otra manera de lidiar con el estrés. 
                            </p>

                            <p>
                                <br>Debemos tomar en cuenta que no todos lidiamos con el estrés de la misma forma, por lo que no debemos compararnos con los demás. Cada quién es distinto y debemos elegir la alternativa o alternativas que mejor se acoplen a nosotros y a nuestro estilo de vida.  De esta forma podremos manejar el estrés adecuadamente y disminuir el riesgo cardiovascular asociado a este.
                            </p>
                            
