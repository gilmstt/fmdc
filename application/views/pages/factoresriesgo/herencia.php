                            <br>¿Alguien en tu familia padece del corazón? Y esto, ¿Cómo te afecta a ti? A continuación, todo sobre el factor herencia en enfermedades cardíacas.
                        </p>
                            
                        <P>
                            <br>La historia y los antecedentes familiares son factores importantes que debemos conocer y tener en cuenta. Esto se debe a que las enfermedades cardiovasculares tienen un factor genético y se pueden “heredar” de la familia directa, como abuelos o padres; del mismo modo, el que algún hermano o hermana tenga una enfermedad cardíaca, aumenta nuestro riesgo de sufrir la enfermedad.
                            <br><br>Una historia familiar de infarto cardiaco, hipertensión o enfermedad coronaria nos predisponen a desarrollar problemas cardiacos, especialmente si se presentaron a una edad temprana.
                            <br><br>Esto no quiere decir que si alguien de la familia padece o padeció del corazón nosotros o nuestros hijos vamos a desarrollar forzosamente la enfermedad, pero sí implica que tenemos más riesgo de padecerla; por ello nuestros cuidados deben ser mayores.
                            <br><br>La herencia y los antecedentes familiares son factores que no podemos cambiar o modificar, pero al identificar que tenemos mayor riesgo de padecer enfermedades del corazón, podemos redoblar esfuerzos para no desarrollarlas.
                            <br><br>Conocer nuestro riesgo nos ayuda para hacer cambios positivos, por lo que hacer ejercicio, dejar de fumar y llevar una dieta cardiosaludable son factores que no debemos perder de vista. Un estilo de vida saludable hará que mejoremos nuestra cantidad y calidad de vida.
                            <br><br>Conoce tu historia familiar, investiga y pregunta sobre enfermedades cardíacas en la familia, es importante para tu salud y la de ellos.

