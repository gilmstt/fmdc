                            <P>
                                <br>Los antecedentes familiares de enfermedad cardíaca son factores de de riesgo  importantes que debemos conocer y tener en cuenta. Se ha demostrado que la historia familiar en línea directa de enfermedad cardíaca aumenta considerablemente el riesgo de padecer enfermedad cardíaca en algún punto de la vida. En otras palabras el contar con  abuelos, padres o hermanos con enfermedad cardíaca, aumenta nuestro riesgo de sufrir enfermedad en este órgano. Este riesgo es específicamente más intenso cuando existe historia familiar de infarto cardíaco a edades tempranas, es decir, antes de los 50 años de edad. 
                                <br><br>Lo anterior no quiere decir que si alguien de la familia padece o padeció del corazón nosotros o nuestros hijos vamos a desarrollar forzosamente la enfermedad, pero sí implica que tenemos más riesgo de padecerla; por ello nuestros cuidados deben ser mayores. 
                                <br><br>La herencia es un factor de riesgo que no podemos cambiar o modificar, pero al identificar que tenemos mayor riesgo de padecer enfermedades del corazón, podemos incidir en mayor medida sobre otros factores de riesgo para disminuir las probabilidades de padecer una enfermedad cardíaca. Algunos factores de riesgo modificables son el estilo de vida, el tabaquismo, los niveles de colesterol, la diabetes mal controlada, entre otros. Estos últimos son potencialmente modificables, es decir, si se controla adecuadamente dicho factor, se disminuyen en cierta medida las probabilidades de padecer la enfermedad. Es por ello que conocer nuestro riesgo nos ayuda para hacer cambios positivos en nuestra vida, como hacer ejercicio, dejar de fumar y llevar una dieta cardiosaludable. 
                                <br><br>Conoce tu historia familiar, investiga y pregunta sobre enfermedades cardíacas en la familia, es importante para tu salud y la de ellos.

