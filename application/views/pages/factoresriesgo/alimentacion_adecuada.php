<br>Llevar una dieta sana a lo largo de la vida ayuda a prevenir la malnutrición en todas sus formas, así como diferentes enfermedades no transmisibles y trastornos. Sin embargo, el aumento de la producción de alimentos procesados, la rápida urbanización y el cambio en los estilos de vida han dado lugar a un cambio en los hábitos alimentarios.
<br><br>En un mundo globalizado, en donde los trabajos se han vuelto absorbentes, la hora de comida es corta, jornadas de trabajo largas y todo mundo tiene prisa, la alimentación a pasado a segundo término. La urbanización a generado que la de alimentos procesados y comida rápida sea la primera opción, ya que ahorra tiempo de preparación y en ocasiones es más económica que elegir una preparación saludable.
<br><br>“Los alimentos procesados industrialmente, las bebidas azucaradas y la comida rápida están desplazando a las dietas tradicionales más nutritivas, lo que genera efectos alarmantes en la salud. Los expertos señalan que se necesita regular el mercado para revertir esta tendencia en América Latina y en todo el mundo.” OPS/OMS

<br><br>Hoy en día las personas consumen más alimentos hipercalóricos, grasas, azúcares libres y sal/sodio; por otra parte, muchas personas no comen suficientes frutas, verduras y fibra dietética, como por ejemplo cereales integrales aunado a que no ingieren agua. Lo cierto es que cada día nos alejamos más de una dieta saludable.
La composición exacta de una alimentación completa, suficiente variada, equilibrada y adecuada e inocua estará determinada por las características de cada persona (edad, sexo, hábitos de vida y grado de actividad física, así como en el caso de presentar alguna patología), el contexto cultural, los alimentos disponibles en el lugar y los hábitos alimentarios.
<br><br>El consumo de los diferentes grupos de alimentos (frutas y verduras, cereales y tubérculos, alimentos de origen animal y leguminosas) es importantes ya que cada uno nos aporta diferentes propiedades que son necesarias para nuestro cuerpo.
<br><br>Las enfermedades cardiovasculares están dentro de las primeras causas de muerte en México, Las ECV tienen diferentes etiologías sin embargo uno de los factores de riesgo más comunes son los malos hábitos alimenticios que hemos venido desarrollando en los últimos años.
<br><br>Cuando un paciente ya es diagnosticado con ECV, el tratamiento nutricional tiene el objetivo de contribuir con la salud del paciente: monitoreando que cuente con un peso adecuado y brindando asesoría y plan nutricional. El plan nutricional estará diseñado de acuerdo a las necesidades y características del paciente integrando cada uno de los grupos de alimentos.
<br><br>• Se recomienda: El consumo de frutas y verduras es importante por su alto contenido de vitaminas, minerales, fibra y antioxidantes se recomienda consumir 5 raciones al día.
<br>• Consumir moderadamente lácteos bajos en grasas, los cereales integrales forman parte importante del aporte de carbohidratos y fibra así como de algunas vitaminas del complejo B.
<br>• Dentro del grupo de alimentos de origen animal es importante mencionar que se recomienda consumir moderadamente carne rojas (magras) aumentar el consumo de pollo y pescado
<br>• Aumentar el consumo de fibra (presente en frutas, verduras, cereales
<br>• Evitar alimentos procesados y bebidas industrializadas
<br>• Evitar el uso de caldo de pollo artificial en su lugar usar hiervas de olor para brindarle sabor a los alimentos
<br>• Evitar el consumo de alimentos procesados y comida rápida

<br><br>Recomendaciones muy simples que contribuyen a un efecto positivo en el paciente. Lo cierto es que son recomendaciones que en general toda la población deberíamos tomar en consideración para prevenir diversas enfermedades.

