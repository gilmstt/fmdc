                        <p>
                            <br>Las válvulas cardiacas tienen la tarea de asegurarse que el flujo de la sangre a través de las cámaras del corazón sea siempre en una sola dirección. Las válvulas pueden dañarse como consecuencia de traumatismos (daño por impacto), por malformaciones desde el nacimiento, por cambios secundarios a la edad, posterior a un infarto o por estados inflamatorios del corazón. Cuando no pueden hacer su labor adecuadamente, se engloban bajo el término de valvulopatías.

                            <br><br>Existen dos lesiones elementales, que puede suceder en cualquiera de las cuatro válvulas. La primera de estas, la estenosisimplica un estrechamiento del área valvular, además de una incapacidad para la apertura de las valvas. El acortamiento del orificio valvular implica que el flujo de sangre se vuelve turbulento al intentar salir, por lo tanto, el músculo tiene que hacer mayor esfuerzo para intentar vaciar el contenido de la cámara cardiaca.

                            <br><br>La segunda lesión recibe el nombre de insuficiencia valvular. Cuando una válvula es insuficiente, permite el flujo retrógrado de la sangre, cuando en realidad debería estar cerrada. También esta situación exige mayor esfuerzo muscular, y termina por distender la cámara afectada.

<h1><br>Válvula mitral</h1>

<br>La válvula mitral se encuentra entre la aurícula y el ventrículo izquierdos. Cuando el corazón bombea sangre, esta se cierra, y al relajarse el corazón, vuelve a abrirse.

<h1><br>Estenosis mitral</h1>

<br><br>Cuando se la apertura de la válvula mitral se reduce mientras el corazón se encuentra relajado, dificulta el adecuado llenado de sangre al ventrículo izquierdo. Se trata de una enfermedad continua que continuará progresando a lo largo de la vida del paciente.

<br><br>Entre los orígenes de este estrechamiento es principalmente la fiebre reumática, aunque existen otras, mucho menos frecuentes, como lupus eritematoso, defectos desde el nacimiento y algunas enfermedades metabólicas.

<br><br>Los síntomas aparecen después de un deterioro gradual, y su severidad es proporcional al grado de obstrucción valvular. Consisten en dificultad para respirar, principalmente durante las noches y al acostarse, así como palpitaciones, dolor sobre el pecho y debilidad.

<br><br>Es habitual que el médico identifique un soplo (sonido sordo de baja intensidad) cuando escucha al corazón utilizando su estetoscopio. Sin embargo, para entablar un diagnóstico definitivo, es mandatario hacer un ecocardiograma y observar las válvulas directamente.

<br><br>El tratamiento curativo requiere de una intervención directa sobre la válvula, ya sea mediante una variante especial de un cateterismo, o mediante cirugía abierta.  En algunos casos, se podrá incluir agentes anticoagulantes, si se sospecha que la estenosis mitral ha causado enfermedades adicional, como la fibrilación auricular.

<h1><br>Insufiencia mitral</h1>

<br><br>El cierre incompleto de la válvula durante la contracción cardiaca permite que un porcentaje de la sangre que bombea el corazón regurgite hacia la aurícula.

<br><br>Existen diversas causas para esta disfunción valvular. Puede también ser efecto de una fiebre reumática, o también prolapso de la válvula, e incluso por ruptura de las cuerdas tendinosas (ante un infarto), en este escenario, la enfermedad se instaura de forma súbita.

<br><br>En la mayoría de los casos, esta enfermedad permanece por mucho tiempo sin ser diagnosticada, puesto que carece de síntoma, al adaptarse el cuerpo gradualmente a ella. El ventrículo izquierdo aumenta de tamaño, y su dilatación es proporcional a la gravedad de la insuficiencia.

<br><br>La mayoría de los pacientes con una insuficiencia mitral no requieren tratamiento, salvo en casos de suma gravedad o de instauración súbita, donde serán manejados con cirugía valvular.

<h1><br>Válvula aórtica</h1>

<br><br>La válvula aórtica se ubica en la salida del ventrículo izquierdo hacia la aorta. Cuando el corazón bombea sangre, esta se abre, y al relajarse el corazón, vuelve a cerrarse.

<h1><br>Estenosis aórtica</h1>

<br><br>La obstrucción de la salida del corazón implica que menos sangre se bombea en cada latido a pesar de que el corazón esté trabajando de más. Al igual que la estenosis mitral, la estenosis aórtica puede ser causada por fiebre reumática, pero también son importantes las malformaciones al nacimiento o cambios degenerativos en personas de edad avanzada.

<br><br>No es habitual la presentación síntomas por mucho tiempo (incluso 10-20 años) aún en casos de estenosis grave. Pero, con el caso del tiempo, puede presentarse como dolor torácico intenso, desmayo y/o insuficiencia cardiaca.

<br><br>También es habitual escuchar un soplo durante el latido, sin embargo, para hacer un diagnóstico preciso hace falta un estudio de imagen, preferentemente un ecocardiograma.

<br><br>El reemplazo de la válvula aórtica es el tratamiento más efectivo, aunque también hay otras modalidades de intervención (aunque todas requieren cirugía o cateteterismo especializado).

<h1><br>Insuficiencia aórtica</h1>

<br><br>Una válvula incompetente, que permite el retorno de flujo desde la aorta hacia el corazón, designa la insuficiencia aórtica. Habitualmente una consecuencia de la fiebre reumática, aunque en personas con válvulas protésicas este defecto es otra causa común, además de infecciones y traumatismos. En los casos de ser súbita, puede causar arritmias, colapso circulatorio, y, eventualmente, la muerte.

<br><br>En los casos de instauración gradual, suele carecer de síntomas por mucho tiempo, a excepción de un sigiloso soplo. Es hasta etapas más tardías donde la falta de aire, sobre todo ante los esfuerzos y al acostarse, comienza a hacerse presente. Al igual que en las valvulopatías mencionadas previamente, se hace uso de un ecocardiograma para entablar el diagnóstico.

<br><br>La cirugía valvular, tratamiento curativo, se reserva para etapas tardías de la enfermedad en donde los síntomas no consiguen erradicarse con fármacos y estilo de vida sano.

<h1><br>Válvula tricúspide</h1>

<br><br>La válvula tricúspide se encuentra entre la aurícula y el ventrículo derechos. Cuando el corazón bombea sangre, esta se abre, y al relajarse el corazón, vuelve a cerrarse. La afección valvular del corazón derecho es mucho menos habitual que en el corazón izquierdo (válvulas mitral y aórtica), la severidad suele ser menor, reduciendo el número de intervenciones quirúrgicas para estas válvulas. En adición, suelen estar asociadas a daños valvulares del laldo izquierdo.

<h1><br>Estenosis tricuspídea</h1>

<br><br>El orificio valvular se reducido debido a alteraciones de nacimiento o incluso por fiebre reumática, aunque no suele estar aislada en este contexto, sino que también se encuentra afectada la válvula mitral.

<br><br>No suele presentar síntomas, excepto en estados muy graves puede presentarse con acúmulo de líquido en las extremidades y en el abdomen, que condiciona aumento de peso. Al igual que las previas afecciones valvulares, su tratamiento definitivo requiere una reparación valvular directa, que suele hacerse mediante un cateterismo especial.

<h1><br>Insuficiencia tricuspídea</h1>

<br><br>Secundario a infecciones, malformaciones de nacimiento o secuelas de fiebre reumática, la válvula podría permitir flujo reverberante. Los síntomas de congestión (acúmulo de líquido en extremidades y abdomen y el aumento de peso) son similares a aquellos secundarios a la estenosis tricuspídea, por lo que es importante la revisión ecocardiográfica para identificar la causa. La reparación de la válvula tricuspídea por insuficiencia  (reservada para casos de severidad) difiere de las intervenciones previas, pues en esta enfermedad se coloca un anillo alrededor de la válvula, para disminuir el flujo de sangre que reentra.

<h1><br>Válvula pulmonar</h1>

<br><br>La válvula pulmonar se ubica en la salida del ventrículo derecho hacia el tronco pulmonar, de donde tienen origen las arterias pulmonares. Cuando el corazón bombea sangre, las válvulas pulmonares permiten el flujo de sangre, y al relajarse el corazón, lo impiden.

<h1><br>Estenosis pulmonar</h1>

<br><br>El retorno de sangre por la válvula pulmonar mientras el corazón se relaja es muy inusual, más aún en el adulto. En las pocas situaciones que se presenta, suele ser por alguna malformación de nacimiento.

<br><br>Al ser muy inusual que causa alteraciones severas capaces de suscitar síntomas, sólo puede sospecharse por un soplo, y confirmarse por un ecocardiograma. El tratamiento a través de un cateterismo con dilatación suele ser suficiente, es decir, una cirugía es prácticamente innecesaria.

<h1><br>Insuficiencia pulmonar</h1>

<br><br>Las infecciones, malformaciones y daño degenerativo son los responsables de que la válvula se vuelva permeable durante su cierre. Así como en la estenosis pulmonar, los síntomas están ausentes, y el diagnóstico requiere un ecocardiograma  y no suele realizarse tratamiento alguno.

<br><br>PARA RECORDAR…

<br>- Hay dos tipos de lesiones principales en las válvulas: estenosis (su apertura está comprometida) e insuficiencia (su cierre está comprometido)
<br>- Las alteraciones valvulares izquierdas son de mayor frecuencia
<br>- Las secuelas de la fiebre reumática son un riesgo importante para las válvulas del corazón
<br>- Los problemas valvulares pueden producir soplos
<br>- El diagnóstico requiere primeramente un ecocardiograma
<br>- Para el tratamiento, se requiere manipular las válvulas, ya sea mediante un cateterismo, o una cirugía abierta

