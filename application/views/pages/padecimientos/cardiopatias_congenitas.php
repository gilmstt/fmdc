                        <h6><span><i class="fas fa-heart"></i></span>Cardiopatías Congénitas</h6>
                    </div>
                    <div class="row " style="text-align: justify;">
                        <p>
                            Escrito por el Dr. Alejandro López Leija. Medicina Interna.
                        </p>
                        
                        <p>
                            <br>Un defecto cardíaco congénito ocurre cuando el corazón o los vasos sanguíneos próximos al corazón, no se desarrollan adecuadamente antes de nacer. Este tipo de afección cardíaca es un defecto o anomalía, más que una enfermedad, sin embargo, no deja de ser potencialmente mortal.

                            <br><br>Se considera que de cada 100 recién nacidos vivos, entre 3 y 4 presentará una malformación al nacer. De todas las diferentes malformaciones, las cardiopatías congénitas son las segundas en frecuencia en México, detrás de las malformaciones del sistema nervioso central. El riesgo de presentar una malformación cardiovascular aumenta entre mayor prematuridad tenga el recién nacido.

                            <br><br>La causa de la gran mayoría de las cardiopatías no puede limitarse a una sola causa, como una mutación genética, sino que tienen una amplia gama de causas (multifactorial). Es importante enfatizar en que el riesgo que una cardiopatía se vuelve a presentar en un nuevo bebé es baja (3-5%), aunque hay ciertos tipos que ocurren con mayor o menor frecuencia.

                            <br><br>Dado que son prácticamente incontables las diferentes cardiopatías congénitas, sólo se describirán las 4 más frecuentes en nuestro medio:

    <p class="sangria"><br>1. Conducto arterioso persistente</p>

    <br><br>Consiste en un agujero permeable entre la aorta (la arteria principal del cuerpo) y el tronco pulmonar (arterias pulmonares). Es un remanente embrionario, pues esta comunicación es útil mientras el bebé permanece en el vientre. La sangre del feto no requiere ir a los pulmones para recibir oxígeno. Por lo tanto, el conducto arterial permite que la sangre pase de la circulación a los pulmones.

    <p><br>Al momento del nacimiento, la sangre requiere recibir oxígeno en los pulmones, y este orificio se cierra por sí solo. Todos los bebés nacen con este orificio, pero este se estrecha y cierra en los primeros días. Si el conducto arterial fracasa en su cierre natural, la sangre sobrecargará a los pulmones, y no llegará a oxigenar al resto del cuerpo.</p>

    <p class="sangria"><br>2. Defecto de la pared auricular</p>

    <p><br>Una comunicación en pared que separa ambas aurículas, las cámaras superiores del corazón. También denominada “comunicación interauricular”, es parte de la circulación habitual del feto, que está destinada a obliterarse con el nacimiento. Cuando se mantiene en el recién nacido, permite que la sangre rica en oxígeno (proveniente de la aurícula izquierda) se filtre a las cámaras de sangre pobres en oxígeno del corazón (la aurícula derecha). Cuando es pequeña, pueda pasar desapercibida toda la vida. Si es grande, el exceso de sangre que regresa a la aurícula derecha hace que el corazón y los pulmones trabajen demasiado, facilitando un daño gradual de estos órganos.</p>

    <p class="sangria"><br>3. Defecto de la pared ventricular</p>

    <p><br>Igual que el defecto previo, el defecto de la pared ventricular o “comunicación interventricular” se encuentra ahora en la pared que separa ambos ventrículos. Este no es un vestigio de la circulación sanguínea fetal, pero sí es esperado que las paredes se cierren antes del nacimiento, para evitar la mezcla de la sangre desoxigenada (que se limita a las cámaras derechas del corazón) con la sangre oxigenada (que se encuentra únicamente en las cámaras izquierdas). La conexión entre ambos ventrículos implica sobrecarga de trabajo para el corazón, y oxigenación adecuada. Cabe destacar que, como la alteración previa, los síntomas están en función del tamaño de la lesión. Agujeros pequeños podrán mantenerse toda la vida sin molestia alguna.

    <br><br> Estas tres malformaciones son las más frecuentes, y no resulta extraño que se presenten dos o más simultáneamente. En caso de gravedad, todas se reparan con cirugía, así como también todas pueden corregirse espontáneamente, cuando son defectos mínimos.
                            </p>
    <p class="sangria"><br>4. Tetralogía de Fallot</p>

    <p><br>La palabra “tetralogía” es un término griego que quiere decir algo similar a “cuatro palabras”. A pesar de ser conocida desde el siglo XVII, el galeno francés A. Fallot describió los cuatro componentes de esta malformación a finales del siglo XIX:</p>

        <p class="sangria"><br>1. Un defecto en la pared ventricular (ver 3° cardiopatía)
        <br>2. Un impedimento del flujo de la sangre del corazón hacia los pulmones (“atresia pulmonar”)
        <br>3. La aorta, mal posicionada, se encuentra sobre el orificio de salida entre los ventrículos.
        <br>4. El músculo del ventrículo derecho se engruesa demasiado</p>

        <p><br>La obstrucción en la válvula pulmonar (salida del corazón hacia los pulmones) impide que la cantidad habitual de sangre sea bombeada a los pulmones, por lo que mucha sangre pasa a través del defecto de la pared ventricular desde el ventrículo derecho al ventrículo izquierdo y sale por la aorta. En otras palabras, un volumen importante de sangre no pasa por los pulmones, y no es debidamente oxigenada.

        <br><br>Los bebés y niños pequeños con tetralogía no reparada de Fallot a menudo son azules, debido al gran flujo de sangre no oxigenada en circulación. La tetralogía de Fallot se trata quirúrgicamente.
        </p>
 

