                    <div class="row " style="text-align: justify;">
                        <p>
                            Escrito por el Dr. Alejandro López Leija. Medicina Interna.
                        </p>
                        
                        <p style="width: 100% ">
                            <br>El corazón es un órgano vital para vivir, trabaja día y noche sin descanso, pero ¿sabes cómo funciona?
                        </p>
                            
                        <p>
                            <br>El corazón se encuentra en el centro del pecho, entre los pulmones, ligeramente inclinado a la izquierda. Tiene el tamaño aproximado de un puño cerrado y al latir se contrae para bombear sangre a todo el cuerpo.
                        </p>
                        
                        <P>
                            <br>La función principal del corazón es recibir la sangre de todo el cuerpo y bombearla a las diferentes partes del organismo. Inicialmente el corazón, en su porción derecha, recibe toda la sangre que tiene poco oxígeno y la bombea hacia los pulmones en dónde se oxigena. Esta sangre ya oxigenada regresa al corazón, sin embargo a su porción izquierda. Es esta porción del corazón la que bombea la sangre a todo el organismo y con ello provee de oxígeno y nutrientes al resto de los órganosy tejidos que nos conforman. Es por esto que la función del corazón es tan importante.
                        </p>
                        
                        <P>
                            <br>Pero, ¿cómo hace todo esto? Para entenderlo debemos saber cómo es nuestro corazón.

                        </p>
                        
                        <p>
                        <br>Anatómicamente el corazón tiene cuatro cámaras o espacios en donde se acumula la sangre, dos aurículas en la parte superior y dos ventrículos en la parte inferior. A su vez el corazón se divide en dos, la parte izquierda y la derecha, de esta forma el corazón derecho se compone de la aurícula derecha (AD) y ventrículo derecho (VD) mientras que el izquierdo se compone de la aurícula izquierda (AI) y ventrículo izquierdo (VI). Entre estas cámaras o cavidades existen unas válvulas cuya función es regular el paso de la sangre entre las cavidades, cuando estas válvulas no abren o cierran correctamente se presentan los denominados soplos cardiacos.
                        </p>
                        
                        <p>
                            <br>La sangre con poco oxígeno, que proviente de todo el organismo, llega a la aurícula derecha. Inmediatamente pasa al ventrículo derecho gracias a la apertura de la primera de las válvulas conocida como válvula tricúspide.  Ya en el ventrículo derecho la sangre es impulsada hacia los pulmones  pasando por la segunda de las válvulas conocida como válvula pulmonar. La sangre viaja a través de la arteria pulmonar hasta los capilares pulmonares, los cales son una extensa red de diminutos vasos sanguíneos en los cuales se puede llevar a cabo la oxigenación de la sangre cuando pasa por los pulmones. Una vez oxigenada, la sangre regresa al corazón por las venas pulmonares a la aurícula izquierda. Pasa por la aurícula izquierda y fluye hacia el ventículo derecho cuando se encuentra abierta la tercera de nuestras válvulas cardíacas, conocida como válvula mitral. Finalmente, el ventrículo izquierdo, que es la porción con mayor cantidad de músculo y por ello la más fuerte de las cuatro cavidades, se contrae para impulsar la sangre. La sangre fluye a través de la cuarta y última de las válvulas cardíacas, conocida como válvula aórtica, y pasa hacia la principal arteria del cuerpo conocida como aorta. La aorta poco a poco va segmentándose en diferentes ramas que dirigen la sangre hacia todos y cada uno de los órganos y tejidos del cuerpo. 
                        </p>
                        <p>
                            <br>Este proceso permite que el corazón impulse alrededor de unos cuatro a cinco litros por minuto cuando una persona se encuentra en reposo.  Esta cifra puede aumentar significativamente cuando se hace ejercicio o esfuerzo físico. El aumento en la cantidad de sangre bombeada, observado durante la actividad física se debe principalmente a que el corazón tiene la capacidad de aumentar la frecuencia con la que late, es decir, el número de latidos en un minuto; así como, la capacidad de aumentar la fuerza con la que se contrae bombeando mayor cantidad de sangre por cada latido realizado. 
                        </p>
                        <p>
                            <br>Normalmente nuestro corazón late de 60 a 100 veces en un minuto; cuando el corazón se contrae para impulsar la sangre a los pulmones o al resto del cuerpo se le llama sístole mientras que cuando el corazón se relaja para recibir la sangre proveniente del organismo o de los pulmones se le llama diástole; estos términos médicos son importantes de conocer, ya que se usan constantemente.
                        </p>

                        <p>
                            <br>Finalmente es importante mencionar que el corazón, al ser un músculo también necesita recibir sangre con oxígeno y nutrientes. Para ello tiene a las arterias coronarias, éstas nacen de la raíz de la aorta, por donde pasa la sangre completamente oxigenada. Así cuando el corazón está relajado, sin contraerse recibe la sangre que necesita para seguir funcionando. Las arterias coronarias se ramifican con el fin de irrigar la totalidad del corazón, son sumamente importantes ya que gracias a ellas el corazón obtiene la energía para poder latir, cuando una de estas arterias coronarias se tapa u obstruye deja sin sangre oxigenada al corazón provocando un infarto o ataque cardiaco.
                        </p>

