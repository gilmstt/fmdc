                        <h3>Prótesis Valvulares</h3> <br>
                        <p>La contracción cardíaca impulsa la sangre hacia la circulación, en una sola dirección, lo cual permite que todo el organismo reciba liquido y nutrientes.</p>
                        <p>El corazón está dividido en 4 cavidades: 2 aurículas (cavidades de entrada) y 2 ventrículos (cavidades que al contraerse expulsan la sangre hacia la circulación). El corazón, además, está dividido en corazón derecho e izquierdo, a los cuales le corresponden 1 aurícula y 1 ventrículo a cada uno. Las válvulas cardíacas se encuentran entre las cavidades que se han mencionado e impiden que la sangre se desplace en sentido inverso al del flujo normal.</p><br>
                        <p><strong>Existen 4 válvulas cardíacas:</strong></p><br>
                        <ul>
                            <li>Trícuspide y pulmonar (corazón derecho).</li>
                            <li>Mitral y aórtica (corazón izquierdo).</li>
                        </ul><br>
                        <p>Muchas enfermedades, adquiridas o congénitas pueden afectarlas. En nuestro país una de las causas más frecuentes es la fiebre reumática (causada por infecciones recurrentes de la garganta en la infancia) la cual ocasiona daños que impiden que las válvulas se abran o se cierren de manera adecuada.</p>
                        <p>Afortunadamente estas enfermedades pueden tratarse si se acude oportunamente al médico ya sea por medio de procedimientos de cateterismo cardíaco o de cirugía. En esos casos el cirujano puede intentar remodelar la válvula afectada o si esto no es posible puede sustituirla por una prótesis.</p><br>
                        <p><strong>Existen 2 tipos de prótesis valvulares:</strong></p><br>
                        <ul>
                            <li>Mecánicas</li>
                            <li>Biológicas (hechas a partir de tejido bovino o porcino)</li>
                        </ul><br>
                        <p>Existen diversas indicaciones que permiten elegir la válvula más adecuada para cada paciente. Es importante aclarar que las válvulas cardíacas, sobre todo las mecánicas, necesitan tratamiento anticoagulante de por vida ya que tienen una probabilidad muy alta de formar coágulos que pueden hacer disfuncionar la válvula o una vez desprendidos viajar a través de la circulación a otros órganos como el sistema nervioso central Es por ello que se requiere de un estricto apego al tratamiento médico por parte del paciente. Las prótesis biológicas también necesitan anticoagulación habitualmente por 3 meses después de implantadas a menos de que exista alguna otra razón para mantener al paciente anticoagulado.</p>
                        <p>Si tienes algún problema valvular o incluso cuentas con una válvula cardíaca protésica, es importante que acudas a valoración con tu médico cardiólogo.<br />
