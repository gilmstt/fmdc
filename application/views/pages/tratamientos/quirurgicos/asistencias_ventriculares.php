                <h3>Asistencias Ventriculares</h3><br>
                <p>La asistencia ventricular es una forma útil de ayudar o como su nombre lo indica, de asistir al corazón cuando éste no puede cumplir con las demandas del organismo, esto puede suceder en casos de insuficiencia cardiaca o incluso durante una cirugía de corazón.</p><br>
                <p>La asistencia ventricular se lleva a cabo mediante dispositivos de asistencia mecánica, es decir dispositivos que funcionan como bombas que mueven la sangre generando un flujo adecuado para el corazón.<br />
                    Es importante mencionar que un corazón que requiere una asistencia ventricular es un corazón insuficiente, incapaz de manejar por sí mismo el flujo sanguíneo de forma adecuada, por lo que la asistencia ventricular le brinda el soporte hemodinámico que requiere para satisfacer las demandas del organismo, es decir llevar sangre a todo el cuerpo.</p><br>
                <p>Existen distintas formas de ayudar al corazón, por lo que existen diferentes asistencias ventriculares; su uso, así como el tipo de asistencia dependerá del criterio médico, ya que no todos los casos son iguales. Su médico tomará en cuenta distintos elementos como su condición de salud, otras enfermedades y el estado de su corazón.</p><br>
                <p>Todas las formas o tipos de asistencia ventricular mejoran la perfusión o riego sanguíneo, es decir hacen que la sangre llegue adecuadamente a los distintos órganos del cuerpo. También aumentan la cantidad de sangre que llega a nutrir al corazón para que éste pueda seguir funcionando correctamente, reducen el trabajo del corazón y sus demandas de oxígeno ya que hacen la función de bomba y lo ayudan con su “carga laboral”.</p>
                <p>Es importante mencionar que pese a la efectividad y utilidad que tienen las asistencias ventriculares también existen complicaciones y riesgos relacionados a su uso; si usted o algún familiar requiere de una asistencia ventricular comente sus dudas e inquietudes con su médico, solicite toda la información que requiera para entender su uso y funcionamiento.</p><br>
