                            <h3>
                                Marcapasos
                            </h3>
                            <p>¿Para qué sirve? ¿Quiénes deben usarlo y por qué? ¿Conoces a alguien que tenga uno? A continuación, encontrarás información útil sobre marcapasos.</p><br>
                            <h3>
                                ¿Qué es y cómo funciona?
                            </h3> <br>
                            El marcapasos es un dispositivo pequeño que sirve para estimular al corazón y hacer que el ritmo de sus latidos sea normal.<br />
                            La forma en que lo hace es mediante la emisión de impulsos eléctricos, encargados de estimular al corazón para que pueda latir normalmente, pensemos que es como un metrónomo, ya que hace que el corazón no se salga de ritmo.</p>
                            <p>El marcapasos tiene el tamaño aproximado de un reloj de bolsillo y se compone de un generador, encargado de liberar los impulsos eléctricos. Este generador funciona mediante una batería y se coloca mediante un procedimiento quirúrgico por debajo de la piel del pecho.<br />
                                El marcapasos también se compone de cables o electrodos, que por un extremo se conectan al generador y por el otro se implantan en el corazón. Dependiendo del tipo de marcapasos, se enviará la señal eléctrica a una o más zonas del corazón, con el fin de normalizar el ritmo cardiaco.</p> <br>
                            <h3>
                                ¿Quién necesita uno?
                            </h3><br>
                            Esta decisión la debe tomar el cardiólogo, cuando el corazón late a una frecuencia que no es normal; en algunas personas con enfermedad cardiovascular el latido cardiaco puede ser muy rápido o muy lento e incluso irregular, estos trastornos del ritmo o frecuencia pueden generar que el corazón se incapaz de bombear sangre a todo el organismo de forma normal, por lo que el uso de un marcapasos será necesario para corregirlo.</p><br>
                            <h3>

                                ¿Qué debo saber si tengo uno?
                            </h3><br />
                            <p>Lo más importante es seguir las indicaciones que su cardiólogo le haga, no se quede con ninguna duda, pregunte todo lo que no entienda por muy obvio que le parezca, su médico con gusto resolverá sus preguntas.</p>
                            <p>Acuda a sus citas de seguimiento, en ellas el médico podrá revisarlo y verificar el estado de su marcapasos y de su batería, recuerde que la batería de los marcapasos dura en promedio de 5 a 8 años, por lo que después de este tiempo será necesario cambiarla, esto se realiza mediante un procedimiento quirúrgico.</p>
                            <p>Conozca las señales de alarma, contacte a su médico si su frecuencia cardiaca es mayor a 100 latidos por minuto, o si su frecuencia aumenta o disminuye de forma repentina, busque atención médica inmediata si usted tiene dificultad respiratoria, si experimenta desmayos o mareos o si sus piernas y tobillos se hinchan.</p><br>
                            <p>Tenga siempre con usted una tarjeta médica con información de su marcapasos, es decir una identificación que mencione que usted es portador de un marcapasos. Esta información puede llevarla junto con su celular en su bolsillo, cartera o bolsa, incluso puede portar una pulsera o collar, así en caso de emergencia o accidente el personal médico sabrá que usted tiene un marcapasos y cómo actuar ante esta situación. Por ejemplo, usted no deberá someterse a una resonancia magnética ya que puede dañar el funcionamiento del marcapasos.<br />
                                Siempre que se encuentre con algún personal de salud, como enfermeras, dentistas, técnicos o demás personal hospitalario mencione que tiene un marcapasos.</p><br>
                            <p>Usted puede viajar sin problemas, el uso del carro, autobús, tren o aviones no generan problemas con su marcapasos. Ahora bien, cuando usted se encuentre en un aeropuerto o ante cualquier detector de metales, deberá mencionarle al personal de seguridad que tiene un marcapasos, ya que los marcapasos contienen metal y activan el detector de metales. Es importante que sepa que el detector de metales no daña el marcapasos.</p><br>
                            <h3>

                                ¿Qué cosas sí puedo hacer con un marcapasos?
                            </h3><br />
                            Si usted tiene un marcapasos puede hacer actividad física, el ejercicio le ayudará a mantener un estado de salud adecuado, pero recuerde que no debe sobre esforzarse, conozca y respete sus límites.<br />
                            Usted puede bañarse de manera normal e incluso nadar ya que, su marcapasos se encuentra por debajo de la piel y no tendrá contacto con el agua. De igual forma usted puede continuar con su actividad sexual de manera normal ya que no afecta su marcapasos.</p><br>
                            <h4>
                                Puede utilizar sin problemas:
                            </h4>
                            <p>• Tabletas electrónicas y computadoras<br />
                                • Impresoras, escáner y fotocopiadoras<br />
                                • Microondas y electrodomésticos<br />
                                • Planchas de ropa<br />
                                • Secadoras de pelo<br />
                                • Máquinas de afeitar eléctricas<br />
                                • Básculas eléctricas<br />
                                • Radios AM/FM y reproductores de CD/DVD/VHS<br />
                                • Bluetooth y tecnología inalámbrica como módems y routers<br />
                                • Videojuegos incluso inalámbricos<br />
                                • Equipos GPS<br />
                                • Aparatos de rayos X<br />
                                • Taladros eléctricos<br />
                                • Máquinas para afeitar<br />
                                • Controles remotos y transmisores de TV<br />
                                • Conducir un automóvil<br />
                                • Teléfonos celulares, pero se recomienda mantener alejado el celular al menos 15 cm del marcapasos</p><br>
                            <h3>

                                ¿Qué debo evitar?
                            </h3><br />
                            <p>Hay algunas cosas que pueden dañar el funcionamiento de su marcapasos y que debe evitar, por ejemplo, las ondas electromagnéticas interfieren con la señal de su marcapasos y por lo tanto con los latidos de su corazón.</p><br />
                            <h5>A continuación, encontrará lo que debe evitar:</h5>
                            <p>• NO hacer terapia con imanes, usted no debe usar imanes, ni en forma de pulseras, collares o almohadas.<br />
                                • NO usar equipos para soldar<br />
                                • NO permanezca cerca de equipos electrónicos de seguridad<br />
                                • NO someterse a una resonancia magnética<br />
                                • NO manejar o usar equipos pesados<br />
                                • NO utilizar equipos con imanes potentes<br />
                                • NO someterse a radioterapia<br />
                                • NO usar máquinas eléctricas para talar<br />
                                • NO usar maquinaria que genere vibraciones<br />
                                • NO usar estimuladores eléctricos para abdomen<br />
                                • NO utilizar máquinas que calculen la grasa corporal<br />
                                • Evite las cercas o mallas eléctricas<br />
                                • Existen ciertos aparatos usados por dentistas que debe evitar<br />
                                • Los audífonos suelen contener material magnético, por lo que puede interferir con la señal del marcapasos. Para evitarlo manténgalos siempre a 15cm de su marcapasos</p><br>
