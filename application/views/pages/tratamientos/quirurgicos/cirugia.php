                        <h3>Cirugía Cardiaca: Trasplante Cardiaco</h3><br>
                        <p>El trasplante cardíaco se considera en la actualidad, junto a la asistencia circulatoria, el tratamiento más eficaz para mejorar de forma significativa el pronóstico de pacientes con insuficiencia cardíaca en fase terminal, que no es posible de conseguir con tratamientos médicos y/o quirúrgicos convencionales. El número de trasplantes registrados en el mundo es superior a 80.000.</p><br>
                        <p>Según “International Society of Heart and Lung Transplantation” (ISHLT) (2009), la supervivencia media de los adultos que reciben un trasplante cardíaco es superior al 50% a los 10 años, con una vida media de 13 años para los pacientes que consiguen sobrevivir los primeros seis meses tras el trasplante.</p><br>
                        <p>En los últimos años, los resultados del trasplante cardíaco han mejorado progresivamente, especialmente gracias a los avances en la cirugía y la anestesia, los nuevos inmunosupresores y el mejor control de las infecciones, del rechazo (agudo y crónico) y de los tumores.</p><br>
                        <p><strong>Criterios generales de selección del donante para trasplante cardíaco:</strong></p><br>
                        <p>• Diagnóstico de muerte cerebral<br />
                            • Edad menor de 50 años para el hombre y 55 para la mujer<br />
                            • Ausencia de parada cardíaca<br />
                            • Ausencia de enfermedad estructural o traumatismo cardíaco<br />
                            • Ausencia de hipotensión sistólica mantenida (&lt; 70mmHg) durante más de cuatro horas<br />
                            • Ausencia en la infusión de dopamina, dobutamina y/o noradrenalina a dosis elevadas
