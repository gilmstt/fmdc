                                            </header><!-- .entry-header -->
                                            <p ><img class=" wp-image-1207 " align="left" style="margin: 0px 20px 20px 0px " src="<?=base_url("/assets/img/tratamientos/corazon.jpg")?>" alt="" width="248" height="194"/>Es un dispositivo parecido a un marcapasos que controla continuamente el ritmo del corazón. Es utilizado para corregir arritmias del corazón. Se coloca general mente en la parte izquierda del pecho. Si detecta un problema del ritmo que no es demasiado grave, libera una serie de impulsos eléctricos indoloros para corregir el ritmo cardíaco. Si esto no da resultado, o se detecta un problema más grave del ritmo cardíaco, el DAI libera una pequeña descarga eléctrica en el corazón, conocida como “cardioversión” que es una descarga sincronizada con el ritmo que esté presentando.<br />
                                                Si esto no funciona, o se detecta un problema muy grave, como una arritmia muy peligrosa, el dispositivo libera una descarga más potente en el corazón, conocida como desfibrilación, con el fin de detener por completo la arritmia.</p>
                                            <p>El cardiólogo aconseja la implantación de un DAI a diferentes grupos de riesgo. Por ejemplo, a los pacientes que han sufrido una parada cardíaca o algunas otras arritmias ventriculares malignas. También es aconsejable en algunos pacientes que han tenido algún episodio de pérdida de conciencia y tras un extenso estudio se les recomienda la implantación de un DAI por el riesgo que tienen de sufrir un episodio de muerte súbita.<img class="size-full wp-image-1208 " align="right" style="margin: 40px; margin-right: 0px; " src="<?=base_url("/assets/img/tratamientos/desfibrilador.jpg")?>" alt="" width="188" height="112" /></p>
                                            <p>Se suelen utilizar en personas de alto riesgo cardiaco y ritmo del corazón, como arritmias de los ventrículos del corazón, la causa principal de la muerte cardíaca súbita. Se colocan los cables de lectura del DAI en las cavidades del corazón a través de venas que se encuentran debajo de su clavícula generalmente. En algunos grupos de pacientes con insuficiencia cardíaca se ha demostrado que estos dispositivos prolongan la supervivencia.</p><br>
