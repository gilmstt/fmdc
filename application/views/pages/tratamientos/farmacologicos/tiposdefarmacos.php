            <h1>Tipos de Fármacos</h1><br>
            <p>La medicina cardiovascular se caracteriza de todas las especialidades en que tiene varios estudios clínicos en diversos medicamentos que benefician de diferentes maneras al corazón y la presión arterial. A continuación te presentamos los principales grupos, cómo benefician al corazón y algunos ejemplos de medicamentos:</p><br>
            <p><strong>Inhibidores de la enzima convertidora de angiotensina:</strong></p>
            <p>Son los medicamentos más utilizados en cardiología. Principalmente funcionan disminuyendo la presión arterial y también previniendo el crecimiento patológico (inadecuado) del corazón, como en el caso de un corazón que haya sufrido un infarto. Ejemplos: Captopril, Enalapril y Lisinopril.</p><br>
            <p><strong>Antagonistas de los receptores de angiotensina 2:</strong></p>
            <p>Estos medicamentos se utilizan principalmente para reducir la presión arterial y también previenen el crecimiento inadecuado del corazón ante situaciones como infarto. Ejemplos: Losartán, Valsartán y Telmisartán.</p><br>
            <p><strong>Beta bloqueadores:</strong></p>
            <p>Estos medicamentos disminuyen la frecuencia cardiaca, el consumo de oxígeno por el corazón (beneficio en enfermedad coronaria reduciendo el dolor de pecho) y además son excelentes antiarrítmicos. Ejemplos: Metoprolol, Bisoprolol y Carvedilol.</p><br>
            <p><strong>Estatinas:</strong></p>
            <p>Son los mejores medicamentos para reducir los niveles de colesterol LDL y triglicéridos, además de jugar un papel muy importante previniendo infartos ya que estabilizan la placa de colesterol en las arterias coronarias. Ejemplos: Atorvastatina y Rosuvastatina.</p><br>
            <p><strong>Antiplaquetarios:</strong></p>
            <p>Sirven para disminuir eventos cardiovasculares como el infarto miocárdico (corazón) y el infarto cerebral (embolia). El ejemplo más común es la Aspirina, aunque existen otros muy importantes como Clopidogrel, Prasugrel y Ticagrelor.<br /><br>
                Estos son los principales medicamentos usados para la salud cardiovascular.</p>
            <p>Consulta a tu cardiólogo para tener la mejor estrategia de tratamiento.</p>
