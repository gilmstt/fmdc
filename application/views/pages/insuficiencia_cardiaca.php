                        <p>
                            <br>El presente artículo respornderá las principales preguntas relacionadas con la insuficiencia cardíaca. ¿Qué es la insuficiencia cardíaca? ¿Por qué ocurre la insuficiencia cardíaca? ¿Qué factores favorecen que una persona tenga insuficiencia cardíaca? ¿Qué síntomas se presentan al tener insuficiencia cardíaca? ¿Cómo se diagnostica la insuficiencia cardíaca? y finalmente ¿Cómo se trata la insuficiencia cardíaca? 
                        </p>
                        <h1><br>¿Qué es la insuficiencia cardíaca?</h1>
                        <p>
                            Se le conoce como insuficiencia cardíaca al conjunto de síntomas y signos que se producen cuando el corazón no es capaz de bombear adecuadamente la sangre hacia los diferentes tejidos del cuerpo o cuando solo es capaz de realizarlo a expensas de un aumento anormal en las presiones internas del corazón. 
                        </p>
                        <h1><br>¿Por qué ocurre la insuficiencia cardíaca?</h1> 
                        <p>
                        La Insuficiencia cardíaca puede producirse por cualquier alteración estructural o funcional del corazón. Cualquier enfermedad que afecte el corazón puede producir insuficiencia cardíaca, siempre y cuando ella disminuya la capacidad del mismo de bombear la sangre. Entre las principales causas de insuficiencia cardíaca se encuentran :
                        </p>    
                        
                        <p class="sangria">
                               <br> 1.	La cardiopatía isquémica: se produce cuando hay una obstrucción de las arterias que nutren el corazón y con ello una disminución en su flujo sanguíneo al mismo generando una disfunsión del tejido cardíaco. También cuando existen infartos que generan daño irreversible y cicatrización del tejido. 
                               <br><br>2.	Las valvulopatías: la disfunción de las válvulas que dividen al corazón como la insuficiencia mitral o insuficiencia aórtica, entre otras, pueden generar una mala movilización del flujo sanguíneo y con ello generar un aumento en las presiones cardíacas, un aumento en la fuerza que necesita el corazón para bombear la sangre, un aumento en la estasis de la sangre que se propaga en forma retrógrada a la circulación venosa y finalmente una incapacidad para bombear adecuadamente la sangre. 
                               <br><br>3.	La hipertensión pulmonar: puede ser causa o consecuencia. Puede causar aumento de la presión que necesita el corazón derecho para bombear la sangre y con ello generar insuficiencia cardíaca derecha, o puede ser causada por una disfunción cardíaca izquierda con el aumento de las presiones pulmonares. En otro apartado se explica más ampliamente. que puede ser en algunas ocasiones causa de disfunción cardíaca derecha y en otras ocasiones consecuencia de la insuficiencia cardíaca izquierda. 
                               <br><br>4.	Otras causas: otras causas que pueden generar insuficiencia cardíaca son las miocardiopatías, la miocarditis, la amiloidosis, la hipertensión arterial, la enfermedad renal crónica, el síndrome de apnea del sueño, entre otras. 
                        </p>


                        <h1><br>¿Qué factores favorecen que una persona tenga insuficiencia cardíaca?</h1> 
                        <p>
                        <br>Los principales factores de riesgo descritos para insuficiencia cardíaca son similares a los descritos para la enfermedad coronaria y la cardiopatía isquémica. Los principales factores de riesgo cardiovascular son: la hipertensión arterial,  la diabetes mellitus  la obesidad, el colesterol elevado y el tabaquismo. Existen enfermedades que predisponen al desarrollo de insuficiencia cardíaca como lo son: el infarto agudo al miocardio, las enfermedades valvulares crónicas, las cardiopatías congénitas no corregidas, las enfermedades pulmonares crónicas, entre otras. Tener algún familiar con historia de insuficiencia cardiaca es igualmente un factor predisponente, así como la exposición a algunos medicamentos que se han descrito como cardiotóxicos.
                        </p>

                        <h1><br>¿Qué síntomas se presentan al tener insuficiencia cardíaca?</h1> 
                        <p>
                        En su etapa inicial, es infrecuente percibir síntoma alguno, esto debido a que el cuerpo tiene la capacidad de generar mecanismos de compensación que inicialmente revierten la disfunción cardíaca, sin embargo sobrecargando al corazón. Dado que no existen síntomas o que algunas veces la causa no se revierte oportunamente, esta sobrecarga de trabajo sobre el corazón termina con el tiempo siendo perjudicial, empeorando la disfunción cardíaca y progresando a una enfermedad sintomática</p>
                        </p>
                        
                        <p>
                        <br>Los principales síntomas que se presentan son:
                        </p>

                        <p class="sangria">
                                <br>1.	Fatiga y debilidad 
                                <br>2.	Disnea: falta de aire, que puede ser al esfuerzo o en reposo y que comúnmente se puede acentuar al acostarse o durante la noche. 
                                <br>3.	Palpitaciones
                                <br>4.	Edema: acumulación de líquido (hinchazón) en piernas, tobillos y pies
                                <br>5.	Dolor abdominal y falta de apetito 
                                <br>6.	Mareo
                                <br>7.	Tos
                        </p>

                        <h1><br>¿Cómo se diagnostica la insuficiencia cardíaca? </h1> 
                            <p>
                            El diagnóstico es clínico, es decir, lo realiza el médico, tomando en cuenta los síntomas que refiere el paciente, junto con hallazgos complementarios y concordantes en la exploración física. 
                            </p>
                            <p>
                            Se pueden agregar otros estudios para aumentar la certeza del diagnóstico y también para conocer la causa de la insuficiencia cardíaca. Algunos de los estudios que pueden solicitarse son:
                            </p>
                            <p class="sangria">
                            <br>1.	Electrocardiograma (ECG): permite evaluar la actividad eléctrica del corazón y con ello inferir alteraciones en la función del mismo.
                            <br>2.	Estudios de laboratorio: permite conocer los niveles de las principales sustancias de la sangre, así como medir marcadores de congestión vascular que indirectamente pueden ser sugestivos de disfunción cardiaca como son el péptido natriurético cerebral (“BNP” por sus siglas en inglés) o su forma inicial (“pro-BNP”). 
                            <br>3.	Una radiografía de tórax: permite realizar una medición del tamaño y características a grandes razgos de la silueta cardíaca, así como observar datos compatibles con enfermedad pulmonar crónica. 
                            <br>4.	Un ecocardiograma: es un ultrasonido del corazón en el cual se puede evaluar estructural y funcionalmente al corazón; así como medir diferentes parámetros que permiten estimar las presiones del corazón y las velocidades de los flujos sanguíneos que pasan por el mismo. 
                            <br>5.	Prueba de esfuerzo: como su nombre lo indica es una prueba en la que se somete a un paciente a un esfuerzo físico para valorar la tolerancia del paciente al mismo y evaluar el comportamiento del corazón del paciente ante el esfuerzo. 
                            <br>6.	Cateterismo cardiaco: permite la evaluación de la anatomía de las arterias coronarias, mismas que nutren al corazón; así como la medición directa de algunos parámetros como las presiones cardíacas. 
                            </p>

                            <h1><br>¿Cuál es el tratamiento?</h1> 
                            <p>
                            Idealmente, el tratamiento empieza incluso antes de llegar a la insuficiencia cardiaca. Es decir, tan pronto se documentan enfermedades de riesgo, estas se tratan y controlan para evitar que desencadenen la insuficiencia cardiaca.
                            </p>
                            <p>
                            Existen diferentes grupos de medicamentos que se ha probado que tienen eficacia en el tratamiento de la insuficiencia cardíaca, sobre todo cuando se realiza una terapia combinada con los mismos. Estos grupos de medicamentos son: 
                            </p>
                            <p class="sangria">
                                   <br> 1.	Los inhibidores de la enzima convertidora de angiotensina (IECA) como el enalapril, el lisinopril o el captopril, etc.
                                   <br>2.	Los antagonistas del receptor de angiotensina II (ARA-II) como el losartán, telmisartán, candesartán, iversartán, etc. 
                                   <br>3.	Los beta bloqueadores como el metoprolol, bisoprolol, atenolol, etc. 
                                   <br>4.	Los inhibidores de neprilisina como el sacubitril/valsartan 
                                   <br>5.	Los antagonistas del receptor de mineralocorticoides como lla finerrenona, eplerrenona y la espironolactona. 
                                   <br>6.	Otros medicamentos: existen otras clases de medicamentos que en casos seleccionados se pueden utilizar, por lo tanto, su médico precisará el tratamiento farmacológico más adecuado para usted.
                            </p>

