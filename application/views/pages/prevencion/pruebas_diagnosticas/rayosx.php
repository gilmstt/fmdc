                        <p>
                            <br>Los rayos X o radiografía son una forma de radiación electromagnética, similares a la luz visible. Sin embargo, a diferencia de la luz, los rayos X tienen una mayor energía y pueden pasar a través de la mayoría de los objetos, incluyendo el cuerpo. Los rayos X médicos se utilizan para generar imágenes de los tejidos y las estructuras dentro del cuerpo. Si los rayos X que viajan a través del cuerpo también pasan a través de un detector de rayos X al otro lado del paciente, se formará una imagen que representa las “sombras” formadas por los objetos dentro del cuerpo.
                            <br><br>Un tipo de detector de rayos X es la película fotográfica, aunque existen muchos otros tipos de detectores que se utilizan para producir imágenes digitales. Las imágenes de rayos X que resultan de este proceso se llaman radiografías.
                            <br><br>Para crear una radiografía, se coloca a un paciente de tal manera que la parte del cuerpo que se va a examinar se encuentre entre una fuente y un detector de rayos X. Cuando se enciende la máquina, los rayos X viajan a través del cuerpo y son absorbidos en diferentes cantidades por diferentes tejidos, dependiendo de la densidad radiológica de los tejidos por los que pasan.

