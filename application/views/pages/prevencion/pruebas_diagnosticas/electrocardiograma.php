                        <p>
                            <br>El corazón es un órgano que presenta automatismo, es decir por sí mismo se estimula y se contrae para dar lugar a un latido, sin la necesidad de un estímulo nervioso que marque el inicio de cada latido. Para que todas las células del corazón trabajen de forma coordinada y puedan contraerse al mismo tiempo para dar lugar al latido cardíaco, existe un sistema de conducción cardíaca. Este complejo sistema se encarga de la creación y diseminación del estímulo eléctrico necesario para que cada una de las células musculares cardíacas se contraigan en forma coordinada. Cuando existe un estímulo anómalo o una falla en la conducción, se produce un ritmo anómalo de contracción que se conoce como arritmia. 

                            <br><br>El electrocardiograma es un estudio que registra la actividad eléctrica generada por el sistema de conducción cardíaca y lo plasma gráficamente en papel a través de ondas. Por lo tanto, las ondas cardíacas normales se plasman gráficamente de una manera constante, que es identificable y medible. Cuando existe una conducción anormal o una anormalidad cardíaca que altere la conducción cardíaca, esto se registra gráficamente como una alteración de las ondas constantes y se puede identificar. 

                            <br><br>Este estudio es útil en muchas situaciones clínicas. Algunas de ellas son el diagnóstico de las arritmias, el crecimiento de las cavidades cardiacas, el infarto del miocardio, las anormalidades de la conducción cardíaca, el síncope, entre otras. 

                            <br><br>Este estudio se obtiene de manera muy sencilla y rápida. Se coloca a la persona a estudiar acostada boca arriba y se le colocan electrodos autoadheribles en el pecho y las extremidades. Estos se conectan con el equipo de electrocardiografía a través de cables con los cuales el aparato detecta y registra la actividad eléctrica del corazón e inmediatamente lo convierte en un registro gráfico que se plasma en papel. Este estudio no causa molestia alguna y puede ser interpretado inmediatamente por un médico. 


