                            <br>El ecocardiograma es un estudio diagnóstico en el cual se realiza un ultrasonido del corazón. A través del ultrasonido se pueden evaluar la contracción muscular y medir o estimar las presiones o las velocidades del flujo de la sangre en las cavidades cardíacas. Es a través dichas mediciones o estimaciones de diferentes parámetros que se puede evaluar la función cardíaca y apoyar al médico a integrar diagnósticos clínicos. 

                            <br><br>Existen dos modalidades principales de este estudio. La primera de ellas, el ecocardiograma transtorácico. En esta modalidad se realizan mediciones a través del uso de un transductor de ecocardiografía directamente sobre la piel del pecho. Esta modalidad es la más común y no representa ningún riesgo para el paciente. La segunda modalidad del estudio es el ecocardiograma transesofágico.  En esta modalidad se introduce un transductor de ecocardiografía especial por la boca y hacia el esófago (estructura cilíndrica que conecta la boca con el estómago) para realizar mediciones especiales desde otra perspectiva más cercana a ciertas cavidades del corazón. Dado que esta última modalidad se considera invasiva y requiere de sedación para su realización, se reserva para casos específicos donde las mediciones o visualizaciones son indispensables para el diagnóstico. 


