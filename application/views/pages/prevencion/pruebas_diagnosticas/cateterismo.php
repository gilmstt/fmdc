                        <p>
                            <br>El cateterismo cardíaco es un procedimiento invasivo utilizado en cardiología que consiste en introducir una guía dentro del sistema vascular del cuerpo y dirigirla hasta el corazón por medio de fluoroscopia (imágenes por rayos X en tiempo real). Una vez alcanzado el corazón, es posible dirigir dicho catéter/guía hacia las arterias coronarias para valorar su flujo. Incluso es posible realizar procedimientos con dicho catéter como la introducción de pequeños globos que permiten la plastia de las arterias para mejorar su flujo y la introducción de pequeñas prótesis conocidas como stents que una vez colocadas impidan una nueva oclusión en dicha zona. 

                            <br><br>Es un procedimiento tanto diagnóstico como terapéutico. Su principal uso es en las urgencias médicas como los infartos agudos, en los cuales se utiliza para restaurar el flujo sanguíneo cardíaco. En pacientes sin infarto agudo, pero con enfermedad coronaria crónica también se utiliza para hacer diagnóstico y se puede de igual manera restaurar el flujo sanguíneo cuando exista una indicación de ello. 

                            <br><br>Al ser un procedimiento invasivo, requiere una evaluación médica previa por un cardiólogo, quien se encarga de determinar con precisión el beneficio de realizar dicho procedimiento. 

                            <br><br>El tiempo de recuperación de un cateterismo cardíaco puede varias entre 2 días hasta 5 días en promedio dependiendo de la técnica que se utilice. Al ser un procedimiento invasivo, no está exento de riesgos. Las complicaciones más comunes habitualmente son leves como sangrados que pueden ser controlados fácilmente o dolor en el sitio de punción. Sin embargo, existe el riesgo de complicaciones graves como un infarto posterior al procedimiento o la lesión de alguna estructura sobre todo vascular. Es por ello indispensable una buena valoración por parte de un médico experto en cardiología quien determine la necesidad del cateterismo.


