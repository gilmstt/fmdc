                        <p>
                            <br>El cateterismo cardíaco es un procedimiento invasivo utilizado en cardiología que consiste en introducir una guía dentro de las arterias coronarias para así poder destaparlas cuando éstas se encuentren enfermas, por ejemplo, en caso de Infarto miocárdico agudo causado por la ruptura de una placa de colesterol en las arterias coronarias. Con esto, el cardiólogo intervencionista puede colocar un stent (una prótesis metálica) que previene que vuelva a taparse la arteria.

                            <br><br>Por lo general se utiliza en casos de emergencias como infartos o alteraciones arteriales como disección (desgarro) de la arteria aorta, que es la arteria más importante que sale directamente del corazón. También se utiliza para hacer diagnóstico de enfermedad arterial coronaria (taponamiento coronario por placas de colesterol) cuando existe angina (dolor) de pecho.

                            <br><br>Antes de realizar un cateterismo cardíaco, es necesaria una amplia evaluación de tu cardiólogo para valorar si te encuentras en las condiciones óptimas para realizarlo sin que ocurra alguna complicación que pueda prevenirse.

                            <br><br>El tiempo de recuperación de un cateterismo cardíaco puede varias entre 2 días hasta 5 días dependiendo de la técnica que se utilice.

                            <br><br>Recuerda que para evitar padecer de Enfermedad Arterial Coronaria es necesario tener una dieta baja en grasas y carbohidratos, realizar actividad física al menos 30 minutos diarios y evitar fumar.

