                        <p>
                           <br>Introducción:
                        <p>
                           <br>La función del corazón es bombear la sangre a los tejidos, lo cual realiza de forma
                           regular y rítmica. Para lograr bombear la sangre de forma adecuada y para mantener un ritmo
                           constante, el corazón tiene un sistema de conducción propio caracterizado por células
                           especializadas en generar y transmitir impulsos desde puntos específicos en hacia el resto
                           del corazón en forma ordenada y progresiva; de tal manera que cada latido cardíaco para ser
                           eficaz requiere de una conducción cardíaca ordenada que le permita al resto de las células
                           del corazón contraerse de forma coordinada. En personas sanas el impulso cardíaco se origina
                           en un área específica conocida como nodo sinusal y se conduce hacia el resto del corazón por
                           el sistema de conducción cardíaca que asemejan cables eléctricos que propagan el impulso de
                           forma escalonada al resto de las células. Cuando existe una falla que impide el correcto
                           funcionamiento del sistema de conducción cardíaco, se conoce como arritmia. Exiten múltiples
                           tipos de arritmias con fisiopatologías, tratamientos y pronósticos propios.
                        </p>

                        <h1><br>¿Qué es la fibrilación auricular? </h1>

                        <p>
                           <br>La fibrilación auricular es un tipo de arritmia que como ya hemos dicho, es una
                           enfermedad del corazón que implica una falla en el sistema de conducción cardiaco. La
                           fibrilación auricular se caracteriza por la formación de múltiples impulsos desordenados
                           provenientes de las aurículas que generan un ritmo acelerado e irregular, así como una
                           ausencia de una contracción auricular eficaz dibido a la presencia ¡ de múltiples impulsos
                           generados al mismo tiempo lo que impide una adecuada coordinadación de las células
                           contráctiles de las aurículas.
                        </p>

                        <h1><br>¿Cómo se produce la fibrilación auricular?</h1>

                        <p>
                           <br> Al tratarse de una arritmia, conlleva una falla en el sistema de conducción cardíaca. En
                           el caso de la fibrilación auricular, el fallo radica en las aurículas (cámaras superiores del
                           corazón), en las cuales se producen impulsos eléctricos anormales totalmente externos al del
                           circuito eléctrico habitual. Estos impulsos se incrementan hasta 300-400 por minuto, muy por
                           encima de los generados habitualmente entre 60 a 100 por minuto en el nodo sinusal (principal
                           marcapasos cardíaco que indica el ritmo y la frecuencia en el corazón sano). Se cree que es
                           un estímulo espontáneo generado en alguna zona del tejido cardíaco que no está especializada
                           para la activación del circuito eléctrico del corazón el detonante que lleva a la aparición
                           de otros estímulos que provocan y perpetúan la fibrilación auricular. Inicialmente, los
                           episodios pueden ser esporádicos y desaparecer de forma espontánea, resolviéndose en un par
                           de minutos. Sin embargo, gradualmente se van prolongando cada vez más tiempo. Este estímulo
                           anormal sostenido puede causar causar cambios en la estructura del corazón, incrementando el
                           tamaño de las aurículas y volviéndolas cicatrízales. Cuando esto último sucede, la
                           fibrilación auricular puede ser persistente e incluso permanente o refractaria a medidas
                           habitualmente utilizadas para revertir esta arritmia.
                        </p>

                        <h1><br>¿Cuáles son las consecuencias de la fibrilación auricular?</h1>
                        <p>
                           <br>La fibrilación auricular puede tener consecuencias importantes sobre la salud. La
                           principal de estas es que cuando existe una contracción irregular de las aurículas, esta
                           propicia la fomación de coágulos (trombos) que al desprenderse se conocen como émbolos,
                           mismos que son capaces de viajar por la circulación arterial y alojarse en arterias de menor
                           calibre obstruyendo por completo el flujo de sangre en el territorio irrigado por la arteria
                           obstruída. Por lo tanto, una obstrucción de un territorio arterial genera un cese súbito del
                           suministro de oxígeno y nutrientes con lo que se puede producir un infarto en dichos tejidos.
                           Esto cobra mayor importancia cuando se obstruye una arteria responsable de irrigar alguna
                           zona del sistema nervioso como un área cerebral, generando un infarto cerebral, el cual tiene
                           consecuencias importantes sobre la salud. Se igual manera se puede obstruír la circulación de
                           otros tejidos como los miembros producienco isquemia e incluso provocar que se genere la
                           necesidad de amputación de dicha extremidad.
                        </p>

                        <h1><br>¿Qué factores favorecen que una persona tenga fibrilación auricular?</h1>

                        <p>
                           <br>La fibrilación auricular es una enfermedad que con el paso de los años se ha ido
                           propagando, aumentando el número de pacientes afectados. Este aumento en los casos reportados
                           anualmente se debe a que la edad avanzada predispone a dicho padecimiento y a que se presenta
                           con frecuencia en personas con enfermedades crónicas comunes preexistentes como hipertensión
                           arterial, infartos cardíacos previos, insuficiencia cardiaca y enfermedades valvulares.
                           Además, la obesidad, el tabaquismo y el consumo de alcohol también favorecen la aparición de
                           esta enfermedad.
                        </p>

                        <h1><br>¿Qué síntomas presenta la fibrilación auricular? </h1>
                        <p>
                           <br>La fibrilación auricular puede en muchos casos ser completamente asintomática, sobre todo
                           en casos en los que se presenta de forma persistente. Algunos de los síntomas que se pueden
                           llegar a presentar son palpitaciones, disnea, síntomas clínicos de insuficiencia cardíaca
                           (ver el artículo de insuficiencia cardíaca para más información), presencia de mareo y
                           sensación de desmayo, dolor de pecho, cansancio o poca tolerancia al ejercicio. Los síntomas
                           más graves de la fibrilación auricular se producen cuando existe una complicación de la misma
                           con eventos de tromboembolismo.
                        </p>

                        <h1><br>¿Cómo se hace el diagnóstico?</h1>

                        <p>
                           <br>Para hacer el diagnóstico de fibrilación auricular es necesario demostrar la presencia de
                           una disfunción eléctrica del corazón como lo puede ser a través de electrocardiografía o de
                           un holter (trazo electrocardiográfico de ritmo que se prolonga durante un tiempo
                           establecido). La fibrilación auricular no siempre se logra capturar, sobre todo en aquellos
                           pacientes que presentan una FA paroxística. .
                        </p>

                        <h1><br>¿ Cómo se trata la fibrilación auricular ?</h1>
                        <p>
                           <br>Todo paciente con fibrilación auricular debe de ser evaluado por un médico para
                           determinar riesgos y beneficios de la anticoagulación y de otros tratamientos que tienen como
                           fin disminuir la frencuancia cardíaca y/o controlar el ritmo.
                           <br>La anticoagulación es uno de los pilares de tratamiento debido a que disminuye el riesgo
                           de formación de trombos y embolización de los mismos a lla circulación arterial.
                        </p>

