                        <h6><span><i class="fas fa-heart"></i></span>MIOCARDIOPATÍAS</h6>
                    </div>
                    <div class="row " style="text-align: justify;">
                        <p>
                            Escrito por el Dr. Alejandro López Leija. Medicina Interna.
                        </p>
                        
                        <p>
                            <br>El corazón es un órgano compuesto por músculo cuya función es bombear 
                            la sangre hacia el sistema circulatorio y con ello proveer de oxígeno y nutrientes
                             al cuerpo. Esto lo logra al contraerse y funcionar como bomba. Para lograr esto,
                              necesita que su capacidad de contracción sea adecuada y se mantenga constante. 
                              Existe un grupo de enfermedades que afecta directamente al músculo cardíaco. 
                              Este conjunto de enfermedades se conocen como Miocardiopatías.
                               Es importante remarcar que estas enfermedades se producen directamente 
                               en el músculo cardíaco y por tanto no son resultado de otras enfermedades 
                               como la enfermedad coronaria, la hipertensión arterial, la enfermedad valvular
                                y las cardiopatías congénitas, entre otras. 
                        </p>
                        
                        <p>
                            <br>Existen múltiples clasificaciones de las miocardiopatías.
                             La sociedad europea de cardiología divide a las miocardiopatías por
                              fenotipos morfológicos y funcionales en los que se incluyen los
                               siguientes tipos:
                        </p> 
                       
                        <p class="sangria">
                            <br>•Miocardiopatía hipertrófica
                            <br>•Miocardiopatía dilatada
                            <br>•Displasia arritmogénica del ventriculo derecho
                            <br>•Miocardiopatía restrictiva 
                        </p>
                       
                        <h1 style="width: 100%"><br>Miocardiopatía hipertrófica</h1>
                        <p>
                            <br>La miocardiopatía hipertrófica es una enfermedad común hereditaria que se produce por la presencia de diversas mutaciones genéticas relacionadas con proteínas del sitema contractil de las fibras musculares del corazón. Esta enfermedad se caracteriza por un engrosamiento/crecimiento desproporcionado de las paredes del corazón (es decir, hipertrofia) sin presentar dilatación en los ventrículos. Este crecimiento desproporcionado se produce en ausencia de otras enfermedades que pudieran explicar dicha hipertrofia como lo son hipertensión arterial, enfermedades valvulares o enfermedades que procucen fibrosis miocárdica extensa.
                        </p>

                        <p>
                            <br>Existen dos tipos principales de miocardiopatía hipertrófica: la forma obstructiva que representa alrededor del 70% de los casos, y la forma no obstructiva que representa el otro 30%.  Como se puede inferir, esta división se basa principalmente en la presencia o ausencia de obstrucción del flujo de salida de la sangre al ser bombeada desde los ventrículos.
                        </p>

                        <p>
                            <br>Esta enfermedad se sospecha en pacientes con historia familiar de la misma enfermedad o con historia de muerte súbita en la familia directa. Algunos síntomas que se pueden presentar son dolor torácico, sensación de falta de aire (disnea), síncope (desmayo) y/o palpitaciones. La exploración física puede ser normal o evidenciar la presencia de un soplo sistólico. 
                        </p>
                            
                        <p>
                            <br>Ante la sospecha de esta enfermedad se pueden solicitar estudios para su valoración como un electrocardiograma y un ecocardiograma. En caso de que dichos estudios no sean cocluyentes, se pueden solicitar estudios avanzados como una resonancia magnética cardíaca.
                        </p>
                        <p>
                            <br>En pacientes con esta enfermedad diagnosticada, se recomienda no ser partícipes de ejercicio extenuante y competitivo, sino realizar actividad física aeróbica de baja intensidad. Algunos medicamentos que se utilizan para los pacientes sintomáticos son los betabloqueadores como el metoprolol o el bisoprolol y betabloqueadores como el verapamilo. En algunos pacientes con riesgo de arritmias, se puede necesitar dispositivos cardíacos como cardioversores implantables. 
                        </p>
                            
                        <p>
                            <br>Esta enfermedad puede ocasionar otras enfermedades como insuficiencia cardíaca o fibrilación auricular, temas que se tratan en otro artículo.
                        </p>
                        
                        <h1><br>Miocardiopatía dilatada</h1>

                        <p>
                            <br>La miocardiopatía dilatada es una alteración en el músculo cardíaco que provoca el aumento del diámetro de todas las cámaras cardíacas en ausencia de otras enfermedades que pudieran justificar este cambio en la morfología. Aunque en muchas ocasiones se desconoce la causa, puede existir el antecedente de alguna infección viral. Las manifestaciones clínicas en esta enfermedad se producen por disminución en la contracción cardíaca producida por el cambio de la morfología y con ella la disminución en el aporte sanguíneo al cuerpo 
                        </p>
                        <p>
                            <br>Se debe de sospechar de esta enfermedad en pacientes que desarrollan signos y síntomas de insuficiencia cardíaca sin una causa que explique los mismos.  También se puede sospechar en pacientes con historia familiar de esta enfermedad, quienes presentan sintomatología de insuficiencia cardíaca. 
                        </p>

                        <p>
                            <br>Los pacientes con sospecha de miocardiopatía dilatada se pueden valorar con electrocardiograma, ecocardiograma y en ocasiones necesitar estudios avanzados como la resonancia magnética. En esta enfermedad suele ser necesario descartar la presencia de enfermedad coronaria, sobre todo en los pacientes con factores de riesgo cardiovascular. Pueden realizarse pruebas genéticas para determinar la causa y el riesgo de presentar la misma enfermedad en la descendencia, sobre todo en personas con historia familiar. 
                        </p>

                        <p>
                            <br>El tratamiento de esta enfermedad suele ser muy parecido a aquel de la insuficiencia cardíaca de fracción de eyección reducida, utilizando medicamentos como los inhibidores de la enzima convertidora de angiotensina (ej: enalapril), los antagonistas del receptos de angiotensina (ej: valsartán), inhibidores del receptor de angiotensina-neprilisina (ej: sacubitril/valsartán), los antagosnitas de receptor de mineralocarticoides (Ej: eplerrenona) y los inhibidores de SGLT2 (ej: empagliflozina).
                        </p>

                        <h1>Miocardiopatía restrictiva: </h1>
                        <p>
                            <br>La miocardiopatía restrictiva se caracteriza por alteraciones que impiden la relajación adecuada del corazón que dificultan el llenado con sangre previo a que se produzca la contracción. En esta enfermedad suele no afectarse el porcentaje de sangre que se bombea fuera del corazón, o el grosor de la pared cardíaca.
                        </p>
                        <p>
                            <br>Esta enfermedad se puede deber a muchas causas, sin embargo, la mayoría de las veces no se identifica una causa desencadenante. Algunas etiologías que pueden relacionarse con esta enfermedad son las enfermedades por depósito como la amoloidosis cardíaca o la sarcoidosis cardiaca. 
                        </p>
                        <p>
                            <br>Esta enfermedad se puede sospechar en pacientes que presentan signos y síntomas de insuficiencia cardíaca, en los cuales existe una fracción de eyección normal. Se puede sospechar tambien en pacientes con antecedentes de radiación terapéutica, quimioterapia o alguna otra enfermedad sistémica 
                        </p>
                        <p>
                            <br>El abordaje de esta enfermedad generalmente requiere la realización inical de un ecocardiograma y si existe sospecha de enfermedades por depósito se recomienda la realización de una resonancia magnética. Existen situaciones en donde el diagnóstico definitivo de la causa requiere la realización de un estudio histopatológico, sobre todo si la sospecha es para una enfermedad por depósito. 
                        </p>
                        <p>
                            <br>El tratamiento de esta enfermedad habitualmente considera el uso de diuréticos como los diuréticos de asa (Ej: furosemida) y los antagonistas del receptor de aldosterona (Ej: eplerrenona) y, en el caso de que se trate de una enfermedad por depósito, requerirá de seguimiento estrecho y tratamiento especializado. 
                        </p> 
                        
                        <p>
                        <br>Conclusión: 
                             <br>Las miocardiotías son un conjunto de enfermedades que se caracterizan por alteración en la mofología cardíaca sin otra causa que pudiera justificar este cambio. 
                              
                        </p>   
                        <p>
                            <br> Si tienes algún síntoma como falta de aire al realizar la actividad física que normalmente tolerabas bien, dolor de pecho con el esfuerzo, desmayos con pérdida del estado de consciencia o antecedentes en tu familia de muerte cardíaca a edades tempranas, puedes padecer una Miocardiopatía. Acude a tu cardiólogo para valoración.
                        </p>
