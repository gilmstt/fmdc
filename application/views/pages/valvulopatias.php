                     <div class="col-lg-12">
                        <p>
                           Escrito por el Dr. Alejandro López Leija. Medicina Interna.
                        </p>

                        <p>
                           <br>Como ya se ha dicho en otros artículos, el corazón se encuentra dividido en 4 cavidades.
                           Las 2 cavidades superiores se conocen como aurículas y las 2 cavidades inferiores que se
                           conocen como ventrículos. Las válvulas cardíacas son tejidos que se encuentran
                           entre las cavidades superiores e inferiores; así como, entre los ventrículos y la arteria
                           pulmonar y la aorta. En la ciruculación de la sangre normal, la sangre retorna de las venas
                           a la primera cavidad que es la aurírcula derecha y pasa a través de la primera válvula
                           conocida como válvula tricúspide hacia una de las cavidades inferiores conocida como
                           ventrículo
                           derecho. Después la sangre se bombea fuera del corazón, pasando por la segunda válvula
                           conocida
                           como válvula pulmonar. La sangre pasa por las arterias pulmonares y se dirige a los capilares
                           en los que se oxigena posterior a lo cual se dirige de regreso al corazón, sin embargo, a las
                           cavidades izquierdas. La sangre regresa al corazón y llega a la aurícula izquierda, pasa al
                           ventrículo izquierdo a través de la válvula mitral. El ventrículo izquierdo bombea la sangre
                           hacia el resto del cuerpo. La sangre bombeada sale del corazón a través de la válvula
                           aórtica.
                        </p>

                        <p>
                           <br>Las válvulas cardiacas tienen la tarea de asegurarse que el flujo de la sangre a través
                           de las
                           cámaras del corazón sea siempre en una sola dirección. Se abren y cierran de acuerdo a la
                           fase en
                           la que se encuentre el ciclo cardíaco. Las fases que tiene el corazón se conocen como sístole
                           (contracción) y diástole (relajación). Las válvulas pueden dañarse como consecuencia de
                           múltiples
                           causas entre las que se encuentran traumatismos (daño por impacto), por malformaciones desde
                           el
                           nacimiento, por cambios secundarios a la edad, posterior a un infarto o por estados
                           inflamatorios
                           del corazón. Cuando no pueden hacer su labor adecuadamente, se engloban bajo el término de
                           valvulopatías. Existen dos lesiones elementales, que puede suceder en cualquiera de las
                           cuatro
                           válvulas. La primera de estas, la estenosis implica un estrechamiento del orificio por donde
                           pasa
                           la sangre, haciendo que el flujo a través de ella sea más difícil y turbulento al intentar
                           salir, por
                           lo tanto, el músculo tiene que hacer mayor esfuerzo para intentar vaciar el contenido de la
                           cámara
                           cardiaca. La segunda lesión recibe el nombre de insuficiencia. Cuando una válvula es
                           insuficiente
                           significa que esta permite el flujo retrógrado de la sangre, cuando en realidad debería estar
                           cerrada.
                           También esta situación exige mayor esfuerzo muscular, y termina por distender la cámara
                           afectada.
                        </p>
                        <h1><br>Válvula mitral</h1>
                        <P>
                           <br>La válvula mitral se encuentra entre la aurícula y el ventrículo izquierdos. Cuando el
                           corazón bombea sangre, esta se cierra, y al relajarse el corazón, vuelve a abrirse.
                        </p>

                        <h1><br>Estenosis mitral</h1>
                        <P>
                           <br>La apertura de la válvula mitral ocurre mientras el corazón se encuentra relajado, La
                           estenosis dificulta el adecuado llenado de sangre al ventrículo izquierdo. Esta enfermedad es
                           crónica y continuará progresando a lo largo de la vida del paciente hasta llegar a ser
                           incapacitante.
                           <br><br>La principal causa de estenosis mitral es la fiebre reumática, una enfermedad
                           ocasionada por la bacteria Estreptococo pyogenes en la cual existen múltiples manifestaciones
                           como artritis, exantema y endocarditis con lo que se produce el daño en las válvulas
                           cardíacas. Este daño a las válvulas puede mantenerse asintomático durante años e iniciar con
                           síntomas hasta la vida adulta. Dxisten otras caausas, sin embargo son mucho menos frecuentes.
                           Algunas son el lupus eritematoso, algunos defectos congénitos (desde el nacimiento) y algunas
                           enfermedades metabólicas.
                           <br><br>Como hemos dicho antes, los síntomas suelen aparecer después de un deterioro gradual
                           asintomático. Consisten en dinea (dificultad para respirar o sensación de falta de aire)
                           sobre todo con la actividad física, durante las noches y al acostarse, así como,
                           palpitaciones, dolor sobre el pecho y debilidad.
                           <br><br>Es habitual que el médico identifique un soplo (sonido sordo de baja intensidad)
                           cuando escucha al corazón utilizando su estetoscopio. Sin embargo, para entablar un
                           diagnóstico definitivo, es mandatario hacer un ecocardiograma y observar las válvulas
                           directamente con el mismo.
                           <br><br>El tratamiento curativo requiere de una intervención directa sobre la válvula, ya sea
                           mediante una variante especial de un cateterismo, o mediante cirugía abierta. En algunos
                           casos, se podrá incluir agentes anticoagulantes, si se sospecha que la estenosis mitral ha
                           causado enfermedades adicionales, como la fibrilación auricular.

                        </p>

                        <h1><br>Insuficiencia mitral</h1>

                        <p>
                           <br>El cierre incompleto de la válvula mitral durante la contracción de los ventriculod
                           cardiacos permite que un porcentaje de la sangre que bombea el corazón regurgite hacia la
                           aurícula. A esta alteración de le conoce como insuficiencia mitral.
                           <br><br>Existen diversas causas para esta disfunción valvular. Puede también ser efecto de la
                           fiebre reumática. Otras causas incluyen el prolapso de la válvula, e incluso la ruptura de
                           las cuerdas tendinosas (cuerdas fibrosas que fijan la válvula hacia el ventrícul) como
                           resultado de un infarto. En este último escenario, la enfermedad se instaura de forma súbita
                           con síntomas que pueden ser graves incluso pudiendo provocar una insuficiencia cardíaca
                           aguda.
                           <br><br>En la mayoría de los casos, esta enfermedad permanece por mucho tiempo sin ser
                           diagnosticada, puesto que la mayoriía de las ocasiones carece de síntomas debido a que el
                           cuerpo se adapta gradualmente a ella. El ventrículo izquierdo tiende a aumentar de tamaño de
                           forma compensatoria siendo esta dilatación proporcional a la gravedad de la insuficiencia.
                           <br><br>El diagnóstico se realiza al igual que otras valvulopatías a través del
                           ecocardiograma (ultrasonido cardíaco) que nos permite identificar el flujo regurgitante a
                           través de la válvula durante la contracción ventricular.
                           <br><br>La mayoría de los pacientes con una insuficiencia mitral no requieren tratamiento,
                           salvo en casos de suma gravedad o de instauración súbita, donde serán manejados con cirugía
                           valvular.

                        </p>

                        <h1><br>Válvula aórtica</h1>
                        <p>
                           <br>La válvula aórtica se ubica en la salida del ventrículo izquierdo hacia la aorta. Cuando
                           el corazón bombea sangre, esta se abre, y al relajarse el corazón, vuelve a cerrarse.
                        </p>

                        <h1><br>Estenosis aórtica</h1>

                        <p>
                           <br>La obstrucción a la salida de la sangre del corazón en la válcula aórtica implica que el
                           corazón requiere más tiempo y fuerza para bombear la misma. Esto genera una sobrecarga de
                           trabajo sobre el mismo. Al igual que la estenosis mitral, la estenosis aórtica puede ser
                           causada por la fiebre reumática, sin embargo en esta valvulopaqtía también son importantes
                           otras causas como las malformaciones al nacimiento, siendo la más común la aorta bivalva
                           (nacimiento con una malformación de la válvula consistente en tener solo dos valvas en vez de
                           4), o cambios degenerativos en personas de edad avanzada.
                           <br><br>Al igual que otras valvulopatías. Suele iniciar como una enfermedad asintomática y
                           progresar de forma contínua incluso durante 10-20 años, incluso en casos de estenosis grave.
                           Los principales síntomas que sueles desarrollarse con el paso del tiempo son el dolor
                           torácico intenso, síncope (desmayo) y/o insuficiencia cardiaca.
                           <br><br>También es habitual escuchar un soplo durante el latido, sin embargo, para hacer un
                           diagnóstico preciso hace falta un estudio de imagen, preferentemente un ecocardiograma.
                           <br><br>El reemplazo de la válvula aórtica es el tratamiento más efectivo, aunque también hay
                           otras modalidades de intervención percutánea.
                        </p>

                        <h1><br>Insuficiencia aórtica</h1>
                        <p>
                           <br>Esta enfermedad se caracteriza por una válvula incompetente, que permite el retorno de
                           flujo desde la aorta hacia el corazón.
                           <br><br>Habitualmente causada la fiebre reumática, además de otras infecciones y
                           traumatismos. En algunos casos, puede presentarse de forma súbita causando arritmias, colapso
                           circulatorio, y, eventualmente, la muerte.
                           <br><br>En los casos de instauración gradual, suele carecer de síntomas por mucho tiempo. El
                           único signo de enfermedad puede ser la presencia de un soplo. Es hasta etapas más tardías
                           donde la falta de aire, sobre todo ante los esfuerzos y al acostarse, comienza a hacerse
                           presente. Al igual que en las valvulopatías mencionadas previamente, se hace uso de un
                           ecocardiograma para entablar el diagnóstico.
                           <br><br>La cirugía valvular, tratamiento curativo, se reserva para etapas tardías de la
                           enfermedad en donde los síntomas no consiguen erradicarse con fármacos y estilo de vida sano.
                        </p>

                        <h1><br>Válvula tricúspide</h1>
                        <p>
                           <br>La válvula tricúspide se encuentra entre la aurícula y el ventrículo derechos. Cuando el
                           corazón
                           bombea sangre, esta se cierra para evitar el reflujo de la misma a la aurícula, y al
                           relajarse el
                           corazón, se abre para favorecer el llenado ventricular. La afección valvular del corazón
                           derecho
                           es mucho menos habitual que en el corazón izquierdo (válvulas mitral y aórtica), la severidad
                           suele
                           ser menor, reduciendo el número de intervenciones quirúrgicas necesarias para estas válvulas.
                           En adición, suelen estar asociadas a daños valvulares del laldo izquierdo.
                        </p>

                        <h1><br>Estenosis tricuspídea</h1>
                        <p>
                           <br>Esta enfermedad se caracteriza por una apertura incompleta de la válvula tricuspídea en
                           la cual el orificio valvular se ve reducido. Las principales causas de esta enfermedad suelen
                           ser de nacimiento, aunque es posible que sea causada también por fiebre reumática. En el
                           contexto de la enfermedad causada por la fiebre reumática, habitualmente no se presenta de
                           forma aislada, sino que se asocia también con afección de la válvula mitral.
                           <br><br>Clínicamente suele ser asintomática. En estados muy graves puede presentarse con
                           presencia de sobrecarga venosa sistémica, es decir disminución del flujo venoso con
                           estancamiento del mismo. Esto provova acúmulo de líquido en las extremidades y en el abdomen.
                           <br><br>Al igual que las previas afecciones valvulares, su tratamiento definitivo requiere
                           una reparación valvular directa, que suele hacerse mediante un cateterismo especial.
                        </p>

                        <h1><br>Insuficiencia tricuspídea</h1>
                        <p>
                           <br>La insuficiencia tricuspídea es el cierre incompleto de la válcula con el mismo nombre.
                           Suele aparecer secundario a infecciones, malformaciones de nacimiento o secuelas de fiebre
                           reumática. Los síntomas de congestión (acúmulo de líquido en extremidades y abdomen y el
                           aumento de peso) son similares a aquellos secundarios a la estenosis tricuspídea, por lo que
                           es importante la revisión ecocardiográfica para identificar la causa.
                           <br><br>La reparación de la válvula tricuspídea por insuficiencia (reservada para casos de
                           severidad) difiere de las intervenciones previas, puesto que en esta enfermedad se coloca un
                           anillo alrededor de la válvula, para disminuir el flujo de sangre que reentra.
                        </p>

                        <h1><br>Válvula pulmonar</h1>
                        <p>
                           <br>La válvula pulmonar se ubica en la salida del ventrículo derecho hacia el tronco
                           pulmonar, de donde tienen origen las arterias pulmonares. Cuando el corazón bombea sangre,
                           las válvulas pulmonares se abren permitiendo el flujo de sangre, y al relajarse el corazón,
                           se cierran con lo cual lo impiden.
                        </p>

                        <h1><br>Estenosis pulmonar</h1>
                        <p>
                           <br>El impedimiento al flujo a través de la válvula pulmonar es inusual, más aún en el
                           adulto. En las pocas situaciones que se presenta, suele ser por alguna malformación de
                           nacimiento.
                           <br><br>Al ser muy inusual, pocas veces causa alteraciones severas capaces de suscitar
                           síntomas. Puede sospecharse por un soplo, y confirmarse por un ecocardiograma. El tratamiento
                           a través de un cateterismo con dilatación suele ser suficiente, es decir, una cirugía es
                           prácticamente innecesaria.
                        </p>

                        <h1><br>Insuficiencia pulmonar</h1>
                        <p>
                           <br>Las infecciones, malformaciones y daño degenerativo son los responsables de que la
                           válvula se vuelva permeable durante su cierre. Así como en la estenosis pulmonar, los
                           síntomas están ausentes, y el diagnóstico requiere un ecocardiograma. Suele no realizarse
                           tratamiento alguno.
                        </p>

                        <h1><br>PARA RECORDAR…</h1>
                        <p class="sangria">
                           <br>• Hay dos tipos de lesiones principales en las válvulas: estenosis (su apertura está
                           comprometida) e insuficiencia (su cierre está comprometido)
                           <br>• Las alteraciones valvulares izquierdas son de mayor frecuencia
                           <br>• Las secuelas de la fiebre reumática son un riesgo importante para las válvulas del
                           corazón
                           <br>• Los problemas valvulares pueden producir soplos
                           <br>• El diagnóstico requiere un ecocardiograma
                           <br>• Para el tratamiento, se requiere manipular las válvulas, ya sea mediante un
                           cateterismo, o una cirugía abierta
                        </p>
