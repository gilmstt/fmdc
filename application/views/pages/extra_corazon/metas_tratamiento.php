                        <h1>Metas de tratamiento de la hipertensión arterial. </h1>
                        <p>
                           <br>Cuando una persona vive con hipertensión arterial debe conocer las metas de control a las que se quiere llegar con el tratamiento que recibe.
                        </p>

                        <h1>¿Cuáles son las metas de tratamiento de la hipertensión?
                        </h1>
                        
                        <p>Las metas a las que se busca llegar durante el tratamiento de la hipertensión habitualmente son las mismas en todos los pacientes (metas estándar). Sin embargo; en algunos casos específicos, estas pueden cambiar haciéndose más o menos rigurosas de acuerdo con las características propias de cada paciente. </p>
                        
                        <h1 class="sangria">- Metas estándar: </h1>
                        <P class="sangria">
                        &nbsp;&nbsp;&nbsp; - Presión sistólica: mantenerse en cifras menores de 140 mmHg 
                        </p>
                        <P class="sangria">
                        &nbsp;&nbsp;&nbsp; - Presión diastólica: mantenerse en cifras menores a 90 mmHg.
                        </p>

                        
                        <h1><br>¿Qué otras cosas se deben de monitorizar durante el tratamiento de la hipertensión? </h1>
                        <p>
                        Durante el tratamiento de esta enfermedad los principales factores que se deben de monitorizar son: 
                        </p>

                        <p class="sangria">1. <b>Diabetes:</b> se deben de realizar pruebas para descartar la presencia de esta enfermedad. Esto con el fin de dar tratamiento oportunamente </p>
                        <p class="sangria">2. <b>Tabaquismo:</b> el consumo de tabaco es un importante factor de riesgo para la enfermedad cardiovascular. Se debe de buscar disminuir o evitar el consumo de tabaco siempre que sea posible.</p>
                        <p class="sangria">3. <b>Grasas en sangre:</b> junto con el tratamiento para la hipertensión, es necesario solicitar niveles de las grasas en sangre, es decir colesterol y triglicéridos, para determinar la necesidad de tratamiento.</p>
                        <p class="sangria">4. <b>Nutrición y control del peso:</b> El manejo nutricional y el ejercicio son medidas ideales para llevar un adecuado control del peso. El solo control del peso mejora no solamente el riesgo cardiovascular, sino que ayuda en el control de la presión arterial y de otras enfermedades como la diabetes.</p>

                        <p><br>La hipertensión no tiene cura. Sin embargo, se puede controlar de forma eficaz mediante cambios en el estilo de vida y, cuando sea necesario, con medicamentos. Conocer las metas de tratamiento nos ayuda como pacientes a llevar un adecuado control de nuestra enfermedad, e informarle de forma asertiva al médico cuando no estamos teniendo un adecuado control y es necesario un ajuste al tratamiento. </p>
