                        <h1>Angina de pecho </h1>
                        <p>
                           <br>Uno de los principales síntomas de enfermedad cardiovascular es el dolor en el pecho que se conoce como angina. En el presente artículo abordaremos las principales características para identificar este malestar y los pasos a seguir cuando se sospecha de la misma.
                        </p>
                        <h1><br>¿Qué es la angina de pecho? </h1>
                        <p>Se conoce como angina al síntoma caracterizado por dolor, incomodidad u opresión en el pecho que se desencadena habitualmente con la actividad física y mejora con el reposo. Este síntoma puede ser la primera y principal manifestación de enfermedad cardiovascular, en específico de la isquemia cardíaca, que es, en palabras sencillas, la disminución en el aporte de sangre y oxígeno al corazón debido en la mayoría de las ocasiones a una oclusión parcial de las arterias de este órgano por placas de colesterol dentro de las mismas.</p>

                        <h1><br>¿Por qué es importante identificar esta condición? 
                        </h1>
                        
                        <p>
                        Este síntoma representa un desequilibrio entre la demanda y el aporte de oxígeno en el corazón; es decir, es un aviso de nuestro cuerpo cuando no existe un adecuado flujo de sangre al corazón y ello genera un sufrimiento de dicho órgano. 
                        </p>
                        
                        <p><br>Es importante remarcar que no es lo mismo tener angina de pecho que presentar un infarto de corazón. El infarto es una condición en la que existe un daño estructural y muerte celular. Por su parte, en la angina no existe dicho daño sino que solo se produce un sufrimiento en las células del corazón que da a lugar al dolor de pecho. Por ende, este síntoma puede fungir como una alerta de que existe un problema en nuestro corazón que, de no atenderse oportunamente,  puede derivar en el desarrollo de una condición más grave en la que se presente un daño irreparable.
                        </p>
                        <h1><br>¿Cuáles son los síntomas? </h1>
                        <p>El principal síntoma que se presenta es el dolor de pecho. Este habitualmete se describe como opresión intensa (“como si un elefante se sentara encima de mí”); sin embargo puede manifestarse solamente como malestar o incomodidad localizada en el área. Algo característico de esta condición es que aparece cuando se realiza algún esfuerzo físico como caminata, actividades del hogar o ejercicio, entre otras; y que esta desaparece con el reposo. </p>
                        <p><br>Dicho malestar puede estar acompañado de otros síntomas como falta de aire, sudoración e irradiación del dolor a otras partes del cuerpo como el cuello, mandíbula o brazo. </p>
                        
                        <h1><br>¿Qué hacer si se sospecha? </h1>

                        <p>En el momento del dolor lo principal es detener la actividad física y mantenerse en reposo hasta que el malestar desaparezca. Una vez identificado el síntoma es crucial agendar una cita médica con un médico cardiólogo o médico internista para la realización de estudios diagnósticos y determinar el tratamiento necesario según el caso.  </p>
                        
                        <h1><br>¿Cuándo se requiere una atención de urgencia?</h1>
                        <p>Existen algunas características que pueden indicar que el dolor de pecho requiere una valoración de urgencia dado que puede estar ocasionado por un infarto en evolución que requiere un tratamiento rápido. A continuación enumeramos una serie de banderas rojas que pueden ser indicativas de una emergencia cardiovascular.</p>
                        <p class="sangria">1. <b>Inicio reciente en pacientes que nunca lo habían presentado.</b></p>
                        <p class="sangria">2. <b>Cambio en las características del malestar.</b> Un aumento de la frecuencia del dolor o que aparezca con esfuerzos más pequeños puede ser sugestivo de progresión de la enfermedad.
                        </p>
                        <p class="sangria">3. <b>Que el dolor aparezca en reposo.</b> Como se ha dicho la angina aparece con la actividad física. Si el dolor aparece sin haber realizado un esfuerzo físico, se considera una bandera roja  
                        </p>   
                        <p class="sangria">4. <b>Un aumento en la duración del dolor o que el dolor no ceda con el reposo.</b> Eventos mayores a 20 minutos de duración o que no cedan ante el cese de la actividad pueden ser indicativos de progresión de la enfermedad o un infarto.</p>
                        <p>La angina de pecho es uno de los principales síntomas de enfermedad cardiovascular. Si presentas este síntoma, acude con tu médico cuanto antes ya que puede ser una alerta ante la posibilidad de desarrollar un infarto en el futuro.</p>

