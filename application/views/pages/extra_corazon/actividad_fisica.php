                        <p>
                           <br>La actividad física es uno de los pilares fundamentales para mantener y mejorar la salud cardiovascular. El ejercicio regular ayuda a reducir el riesgo de enfermedades cardíacas, mejorar la circulación, disminuir los niveles de colesterol, y controlar la presión arterial. Además, fortalece el corazón y los vasos sanguíneos, haciendo que el sistema cardiovascular sea más eficiente.
                        </p>

                        <h1><br>¿Qué beneficios tiene la actividad física en la salud cardiovascular?</h1>
                        <p>
                           <br>El ejercicio físico aporta numerosos beneficios directos para el corazón y el sistema circulatorio. A continuación enumeramos los principales efectos benéficos en la salud cardiovascular:
                        </p>
                        
                        <p class="sangria">
                           <br>1.	<b>Mejora la función cardíaca:</b> el ejercicio fortalece el corazón, lo que permite que bombee la sangre de manera más eficiente. Esto produce que el corazón requiera de un menor esfuerzo para cumplir con su tarea.
                        </p>

                        <p class="sangria">
                           <br>2.	<b>Reducción de la presión arterial:</b> La actividad física regular ayuda a disminuir tanto la presión arterial sistólica como la diastólica. Esto ayuda a reducir el riesgo de padecer hipertensión. En personas que padecen de dicha enfermedad, la actividad física ayuda a tener un mejor control de la enfermedad.
                        </p>

                        <p class="sangria"><br>3.	<b>Mejora las grasas en la sangre: </b>El ejercicio contribuye a reducir los niveles de colesterol malo (LDL) y a aumentar los niveles de colesterol bueno (HDL), lo que reduce la formación de placas en las arterias y con ello disminuye el riesgo de enfermedad cardiovascular. 
                      </p>
                        
                        <p class="sangria"><br>
                        4.	<b>Control del peso:</b> Mantener un peso saludable disminuye el riesgo de enfermedades cardiovasculares y metabólicas asociadas con la obesidad, como son la diabetes tipo 2, la hipertensión y la dislipidemia (descontrol de las grasas en la sangre).

                        </p>         

                        <p class="sangria">
                           <br>5.	<b>Disminución del riesgo de aterosclerosis:</b> El ejercicio contribuye a mejorar  la circulación sanguínea y reducir la inflamación en el cuerpo. Con ello puede disminuir el endurecimiento de las arterias y disminuir el riesgo de enfermedad cardiovascular.
                        </p>

                        <h1><br>¿Cuánto ejercicio es el recomendado para mantener la salud cardiovascular?</h1>
                        
                        <p>
                           <br>Para obtener los beneficios óptimos para la salud cardiovascular, las principales organizaciones de salud, como la American Heart Association (AHA) y la Organización Mundial de la Salud (OMS), recomiendan los siguientes objetivos de actividad física:
                        </p>
                        
                        <p class="sangria">
                           <br><b>a.	Ejercicio aeróbico:</b> 150 minutos a la semana de intensidad moderada, como caminar rápido o nadar. O bien 75 minutos de ejercicio aeróbico vigoroso, como correr o ciclismo rápido.
                        </p>
                        <p class="sangria">
                           <br><b>b.	Ejercicio de resistencia (entrenamiento de fuerza):</b> Se recomiendan ejercicios de resistencia al menos 2 días por semana, enfocándose en los principales grupos musculares (piernas, caderas, espalda, abdomen, pecho, brazos y hombros).
                        </p>

                        <p>
                           <br>Cumplir con estas metas ayuda a mejorar la salud cardiovascular, reducir el riesgo de mortalidad por enfermedades del corazón y mejorar el bienestar general.
                        </p>

                        <h1><br>¿Qué tipo de ejercicio es mejor realizar? </h1>

                        <p>
                           <br>Se recomienda una combinación de ejercicio aeróbico y de resistencia cumpliendo con las metas de actividad física propuestos por las principales organizaciones mundiales. El ejercicio aeróbico, también conocido como "cardio", es cualquier actividad que aumenta el ritmo cardíaco y la respiración de manera sostenida. Algunos ejemplos de este tipo de ejercicio son la caminata, el correr, la natación, el ciclismo y el baile. Por otro lado, el ejercicio de resistencia es aquel en el que se realiza entrenamiento de fuerza de los principales grupos musculares. 
                        </p>

