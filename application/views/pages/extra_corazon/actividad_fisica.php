                        <h1><br>¿Cómo puedo saber la intensidad del ejercicio que realizo? </h1>

                        <p>
                           <br>Para asegurarte de que estás realizando ejercicio a una intensidad adecuada para obtener beneficios cardiovasculares, existen varias formas de monitorear el esfuerzo:
                        </p>
                        
                        <p class="sangria">
                           <br>1.	<b>Frecuencia cardíaca:</b> La frecuencia cardíaca máxima se estima restando 220 menos la edad. Durante el ejercicio moderado se debe de alcanzar el 50% al 70% de la frecuencia cardíaca máxima, y en el ejercicio intenso entre el 70% al 85%.
                        </p>

                        <p class="sangria">
                           <br>2.	<b>Prueba del habla:</b> En el ejercicio moderado, es posible mantener una conversación, aunque con cierto esfuerzo. En el ejercicio intenso, hablar resulta  difícil, pero no imposible.
                        </p>
                        
