                        <p>
                           <br>El índice de masa corporal (IMC) es una herramienta ampliamente utilizada para evaluar la cantidad de tejido corporal en relación con la altura y el peso de una persona. En palabras sencillas el valor de IMC representa una relación entre el peso de una persona con respecto a su altura. Este índice se calcula mediante la fórmula: <b>IMC = peso (kg) / altura² (m²)</b>. Por ejemplo, si una persona pesa 70 kg y mide 1.75 m, su IMC sería 22.86. 
                        </p>

                        <p>
                           <br>Este índice se utiliza para categorizar a los individuos en diferentes grupos de salud basados en sus valores de IMC de la siguiente manera: 
