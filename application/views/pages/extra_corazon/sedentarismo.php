                        <p>
                           <br>Hablar de sedentarismo, es hablar de un estilo de vida en el cual se carece de una adecuada cantidad de actividad física. Se estima que alrededor de un 60% de la población mundial podría tener una insuficiente actividad física en el día a día, convirtiéndolo en uno de los principales factores de riesgo para padecer una enfermedad cardiovascular y otros padecimientos crónicos como la diabetes o la hipertensión.
                        </p>

                        <p>
                           <br>La inactividad física se asocia con una disminución en la masa muscular y un aumento de la masa grasa corporal. Con ello se disminuye la fuerza corporal y la funcionalidad, sobre todo cuando se vive la etapa del envejecimiento. 
                        </p>
                        
                        <P>
                           <br>El sedentarismo también aumenta el riesgo de padecer enfermedades metabólicas como la obesidad, diabetes y la dislipidemia. Estas pueden tener complicaciones graves en la salud y disminuir considerablemente la calidad y esperanza de vida.
                        </p>

                        <h1><br>¿Cuáles son los beneficios de tener una adecuada cantidad de actividad física? </h1>
                        <P>
                           <br>En los adultos, el tener una adecuada actividad física tiene beneficios sobre la salud física y mental.  En lo físico, se disminuye la mortalidad asociada con las enfermedades del corazón y del sistema vascular. Así como, el desarrollo de padecimientos crónicos como diabetes e hipertensión.

                        </p>

                       <p>
                           <br>Por otra parte, si la persona ya vive con un de estas condiciones de vida, pero realiza actividad física puede tener un mejor control de su enfermedad y por ende menor cantidad de complicaciones.

                        </p>
                        <p><br>
                        En lo que respecta a la salud mental, se ha comprobado que realizar actividad física disminuye la ansiedad, la depresión y mejora la calidad del sueño. 
                        </p>

                        <h1><br>¿Cuánta actividad física se recomienda realizar al día? </h1>
                        <p>
                           <br>En adultos se recomienda que realicen 30 minutos de actividad física aeróbica como: caminar, nadar, trotar; la menos 5 veces a la semana.   
                        </p>

                        <p><br>
                        En caso de realizar actividad intensa se recomiendan al menos 75 a 150 minutos semanales de actividad, es decir 15 minutos al menos 5 veces por semana. 
                        </p>

                        <p>
                           <br>También se recomienda la realización de actividad física de fortalecimiento muscular general en el cual se trabajen todos los grupos musculares al menos 2 veces por semana. 
                        </p>

                        <p><br>
                        Las personas que puedan realizar más de 300 minutos semanales de actividad moderada pueden tener mayores beneficios en la salud cardiovascular.
                        </p>

                        <h1><br>¿Qué pasa si no tengo tiempo?</h1>
                        <p>
                           <br>Si bien las recomendaciones previamente mencionadas tienen tiempos mínimos establecidos de actividad física semanal, existe beneficio en la realización de algo de actividad física, aunque ello no cumpla con las metas establecidas. Siempre es mejor tener algo de actividad que permanecer sedentario. Es por eso por lo que dedicar al menos un poco de tiempo a la realización de actividad física es mejor que no dedicar nada de tiempo. También existe beneficio en sustituir las actividades sedentarias por actividad física al menos leve ejemplo de ello es subir escaleras y caminar distancias cortas.
                        </p>

                        <h1><br>¿Qué pasa si no tolero realizar una adecuada cantidad de actividad física?</h1>
                        <p>
                           <br>Si se busca comenzar a realizar actividad física, siempre se debe de comenzar con pequeñas dosis e ir incrementando gradualmente la duración, frecuencia e intensidad hasta alcanzar las metas. Si padeces alguna enfermedad que te lo impida, acude con un médico para que te informe de qué tipo de actividad se ajusta a tus características. 
                        </p>

                        
