                        <p>
                        La diabetes es una enfermedad metabólica en la que el cuerpo presenta un inadecuado manejo de las azúcares en la sangre debido a una insuficiente cantidad de insulina o la incapacidad del cuerpo de responder a esta misma de forma adecuada. Es importante remarcar que la diabetes es una de las enfermedades más comunes en la actualidad y también una de las que más complicaciones presenta. Esta enfermedad produce daño en diferentes partes del cuerpo, siendo más importante el daño en los vasos sanguíneos, en el corazón, los ojos, los nervios y los riñones
                        </p>

                        <h1><br>Factores de riesgo</h1>
                        <p>Es importante que sepas que, para el desarrollo de la diabetes, existen factores que se pueden cambiar y otros que no se pueden modificar. 
                        </p>
                        <h1>Diabetes (factores de riesgo, síntomas y diagnóstico)
                        </h1>
                        <p class="sangria">a. Factores modificables: </p> 
                        
                        <p class="sangria">&nbsp;&nbsp;- Estilo de vida sedentario: La inactividad física se relaciona con el desarrollo de múltiples enfermedades como las enfermedades metabólicas entre las cuales se encuentra la diabetes. </p>
                        <p class="sangria">&nbsp;&nbsp;- Obesidad: esta enfermedad se asocia con resistencia a la acción de la insulina que es el principal mecanismo por el cual se produce la diabetes. </p>
                        <p class="sangria">&nbsp;&nbsp;- Dieta alta en carbohidratos: al igual que la obesidad, una dieta alta en azúcares se asocia con mayor resistencia a la acción de la insulina.</p>

                        <p class="sangria">b. No modificables: </p> 
                        <p class="sangria">&nbsp;&nbsp;- Susceptibilidad genética: existen algunos factores genéticos identificados que aumentan el riesgo de padecer esta enfermedad. También se ha observado que tener familiares en línea directa afectados (padre, madre, abuelos) aumenta considerablemente el riesgo de padecer esta enfermedad. 
                        </p>
                        <p class="sangria">&nbsp;&nbsp;- Etnicidad: algunos grupos poblacionales mundiales tienen mayor susceptibilidad para el desarrollo de esta enfermedad. Entre ellos se encuentra el ser latinoamericano.</p>

                        <br><h1>Sintomatología</h1>
                        <p>La diabetes puede ser una enfermedad asintomática. En algunas personas esta enfermedad puede pasar desapercibida durante muchos años hasta el desarrollo de complicaciones o el diagnóstico incidental por otras causas. Sin embargo, existen algunos síntomas con relación bien establecida con esta enfermedad. </p>

                        <p><br>Algunos de los síntomas más comunes de esta enfermedad son los siguientes: </p>
                        <p class="sangria">&nbsp;&nbsp;- Cuando una persona orina de forma excesiva <b>(poliuria)</b>. El exceso de azúcar en la orina induce un estado de pérdida de agua con lo cual se aumenta la cantidad de la misma y se va muchas veces al baño.</p>
                        <p class="sangria">&nbsp;&nbsp;- <b>Sed excesiva y consumo de grandes cantidades de agua (polidipsia).</b> La diabetes puede inducir un estado en el cual es necesaria la ingesta abundante de líquidos. </p>
                        <p class="sangria">La diabetes no controlada puede aumentar el hambre y apetito de las personas con lo cual existe un aumento en la ingesta de alimentos respecto a la ingesta previa <b>(polifagia).</b> </p>
                        <p class="sangria"><b>Pérdida de peso: </b> la diabetes no diagnosticada puede provocar en el cuerpo un estado en el cual se consume poco a poco y existe una pérdida importante de peso.</p>
                     
                        <br><h1>Diagnóstico de diabetes: </h1>
                        <p>Para realizar el diagnóstico de diabetes es necesario demostrar la presencia de niveles elevados de glucosa (azúcar) en sangre. Existen diferentes criterios diagnósticos que los médicos utilizan investigar si la persona padece diabetes.</p>
                        <p><br>Se recomienda que todo adulto sano se realice estudios de laboratorio generales al menos 1 vez al año, en los cuales se incluyan estudios para la detección oportuna de diabetes, sobre todo en personas con mayor riesgo. Acude con tu médico para conocer tu estado de salud y llevar el seguimiento adecuado para el diagnóstico y prevención oportunos.</p>
                     
