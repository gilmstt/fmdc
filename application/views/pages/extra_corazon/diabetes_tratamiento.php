                        <h1>Diabetes: metas del tratamiento. </h1>
                        <p>
                           <br>Uno de los principales factores de riesgo para el desarrollo de enfermedad cardiovascular es la diabetes; ya que cuando el paciente no tiene un buen control de la enfermedad aumenta la posibilidad de sufrir complicaciones. 
                        </p>
                        <p><br>Existen diferentes estrategias que se pueden adoptar. </p>

                        <h1><br>Tratamiento no farmacológico
                        </h1>
                        
                        <p>
                           Los cambios en el estilo de vida deben adoptarse por todos los pacientes como base del tratamiento debido a que el éxito de otras medidas, como el tratamiento farmacológico, depende en parte del buen apego a éstas. 
                        </p>
                        
                        <p><br>
                           1. &nbsp;&nbsp; <b>Control del peso:</b> mantener el peso indicado es crucial para el tratamiento de la diabetes debido a que esto puede disminuir los niveles de azúcar en sangre y aumentar la eficacia de los medicamentos. 
                        </p>
                        <p><br>2. &nbsp;&nbsp; <b>Ejercicio:</b> un régimen adecuado de actividad física en el cual se combine ejercicio aeróbico (correr, trotar, bicicleta, natación, etc) con ejercicio de resistencia (fortalecimiento muscular, pesas, etc) ayuda a disminuir los niveles de azúcar en sangre y con ello lograr más fácilmente las metas de tratamiento.</p>
                        <p><br>3. &nbsp;&nbsp; <b>Dieta:</b> una dieta balanceada en la cual se disminuya el consumo de carbohidratos, sobre todo de azúcares simples (dulces, chocolates, pan de dulce, etc),es crucial en el tratamiento. Un mal apego a la dieta puede provocar un fracaso en el terapéutico, incluso con un manejo farmacológico adecuado. Es por ello que un adecuado asesoramiento por profesionales de la nutrición es necesario para el éxito de las medidas. </p>
                     
                        <h1><br>Tratamiento farmacológico</h1>
                        <p>Existen muchos medicamentos para la diabetes. Habitualmente se requiere una combinación de varios fármacos y un adecuado apego a las medidas no farmacológicas para lograr el control de la enfermedad. El tratamiento farmacológico requiere un estrecho seguimiento por parte de un médico quien se encargará de hacer los ajustes pertinentes para lograr el adecuado control.</p>

                        <h1><br>Metas de tratamiento </h1>
                        <p>
                        Una vez realizado el diagnóstico e iniciado el tratamiento de la diabetes es importante conocer las metas a las que se plantea llegar con el mismo. Existen diferentes maneras de valorar el control de la enfermedad y en cada una de ellas existe una meta específica a la que se aspira con el tratamiento.
                        </p>
                        <p class="sangria"><br>
                        1. <b>Hemoglobina glucosilada (HbA1c):</b> se trata de una medición de laboratorio que representa el porcentaje de hemoglobina (proteína de la sangre)  unida a la glucosa, promedio de los últimos 3 meses. A mayores niveles de glucosa (azúcar) en sangre, mayores niveles de HbA1c. La meta es lograr niveles menores o iguales al 7%.j
                        </p>
                        <p class="sangria">
                           2. <b>Glucosa en ayuno:</b> se trata de la medición de la azúcar en sangre en ayuno ya sea por laboratorio o por glucometría capilar. Este se debe de encontrar dentro de rangos entre 80 y 130 como meta en la diabetes.
                        </p>
                        <p class="sangria">
                           3. <b>Glucosa posprandial:</b> es la medición de la glucosa en sangre habitualmente por glucometría capilar a las 2 horas de los alimentos. La meta de glucosa posprandial son valores menores de 180 mg/dl.
                        </p>
                        <p class="sangria">
                           4. <b> Monitorización continua:</b> existen aparatos de monitorización continua de la diabetes que tienen la capacidad de medir la glucosa de forma continua y con ello hacer gráficas de los niveles de glucosa en sangre a través del tiempo. Dichos dispositivos deben utilizarse bajo la supervisión de médicos especialistas dado que la interpretación de los resultados requiere un entrenamiento especializado. 
                        </p>
                       
                        <p><br>
                        Es de crucial importancia el adecuado manejo de la diabetes debido que esta enfermedad puede generar consecuencias importantes en la salud, entre ellas, la enfermedad cardiovascular. Un adecuado control de la diabetes es reponsabilidad del médico y paciente en conjunto. Como paciente, conocer los objetivos del tratamiento es crucial para concientizarse acerca del control de la enfermedad y adoptar las medidas necesarias para conseguirlo.  
                        </p>

