                        <p>
                           <br>La insuficiencia cardíaca es una condición médica en la cual el corazón no puede bombear suficiente sangre para satisfacer las necesidades del cuerpo. Esto no implica que el corazón haya dejado de funcionar por completo, sino que es menos eficiente de lo normal. La disminución en la capacidad del corazón para bombear sangre afecta varios sistemas del cuerpo y puede ser una condición seria y crónica. Es importante remarcar que la insuficiencia cardíaca es el resultado de otras enfermedades que dañan al corazón de forma aguda o paulatina a través del tiempo. En el presente artículo hablaremos de las principales causas de esta condición y los síntomas que se presentan al padecerla.
                        </p>

                        <h1><br>Principales Causas</h1>
                        <p>
                           <br>Son múltiples las enfermedades que tienen el potencial de producir insuficiencia cardíaca pero generalmente se pueden agrupar en las siguientes categorías:
                        </p>
                        
                        <p class="sangria">
                           <br>1.	<b>Enfermedad Coronaria:</b> Es el tipo más común de enfermedad cardíaca, causada por el estrechamiento de las arterias que suministran sangre al corazón debido al depósito de colesterol en las paredes de las mismas.
                        </p>

                        <p class="sangria">
                           <br>2.	<b>Infartos previos: </b>Los ataques al corazón pueden dañar el músculo cardíaco de forma irreversible. Estos daños ocasionan que el corazón no pueda contraerse de forma adecuada en algunas zonas y con ello disminuyen la capacidad para bombear eficientemente la sangre.
                        </p>

                        <p class="sangria"><br>3.	<b>Enfermedad Valvular Cardiaca: </b>cuando las válvulas del corazón no funcionan correctamente, obligan al corazón a trabajar más arduamente de lo que debería. Esto puede generar que, a forma de compensación, el corazón  cambie su estructura. Esto a largo plazo genera cambios irreversibles que disminuyen la eficacia para bombear la sangre. 
                        </p>
                           
                           <p class="sangria"><br>
                           4.	<b>Hipertensión arterial: </b> El manejar presiones elevadas en las principales arterias del cuerpo genera un aumento de la resistencia al bombeo de la sangre, en otras palabras, se sobrecarga la bomba de nuestro cuerpo. Esto fuerza al corazón a contraerse con más fuerza, lo que con el tiempo, altera la estructura del mismo debilitándolo o haciéndolo ineficiente. También el crecimiento excesivo del músculo cardíaco de forma compensatoria impide la adecuada relajación del corazón, disminuyendo la cantidad de sangre que entra y con ello también la sangre que se bombea. 
                        </p>         

                        <p class="sangria">
                           <br>5.	<b>Miocardiopatías: </b> un conjunto de enfermedades que afectan el músculo cardíaco generando alteraciones en la estructura y función de este. 
                        </p>

                        <p class="sangria">
                           <br>6.	<b>Otras enfermedades crónicas:  </b> Diabetes, enfermedades de la tiroides, y obesidad también pueden contribuir al desarrollo de insuficiencia cardíaca.
                        </p>

                        <h1><br>Principales síntomas </h1>
                        
                        <p>
                           <br>El cuadro clínico de la insuficiencia cardíaca puede variar entre personas, sin embargo los , los síntomas más comunes incluyen:
                        </p>
                        
                        <p class="sangria">
                           <br><b>●	Dificultad para respirar (disnea): </b> esta se presenta particularmente durante la actividad física que puede ser incluso de mínimos  esfuerzos en etapas avanzadas de la enfermedad. También de forma clásica la falta de aire se presenta cuando se acuesta el paciente o durante el sueño con un despertar por falta de aire. Algunas personas pueden evitar dormir acostados por esta razón utilizando varias almohadas o soportes con el fin de permanecer sentados durante el sueño.  
                        </p>
                        <p class="sangria">
                           <br><b>●	Aumento de peso y retención de líquidos / edema: </b> debido a una disminución en el aporte de sangre a los riñones estos tienden a retener líquido, el cual puede depositarse en los tejidos blandos y ocasionar hinchazón (edema) de los pies y piernas, así como edema pulmonar y falla respiratoria. 
                        </p>

                        <p class="sangria">
                           <br><b>●	Tos o silbido con flema blanca o rosada:  </b> puede ser indicativo de retención de agua en los pulmones debido a que la sangre que no se bombea de forma eficiente se estanca en los pulmones. 
                        </p>

                        <p>
                           <br>Si tienes alguna de las enfermedades que pueden ocasionar insuficiencia cardíaca o si cuentas con algún síntoma sugerente de esta enfermedad acude a revisión con tu médico. El reconocimiento temprano y el manejo adecuado de las causas subyacentes son fundamentales para minimizar el impacto de la insuficiencia cardíaca, mejorando así la calidad de vida del paciente. 
                        </p>

